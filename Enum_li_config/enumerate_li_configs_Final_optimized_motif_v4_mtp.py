class EwaldPrecomputed:
    """
    Precomputed Ewald interaction matrices for fast per-configuration energy
    evaluation within a fixed stacking sequence.

    Building the EwaldSummation matrix is O((N_Li + N_C)²) and is done ONCE
    per stacking sequence.  Per-configuration energy evaluation is then
    O(n_li_keep²) via vectorised numpy operations instead of a full Ewald sum.

    Energy formula (charges: Li = +1, C = -n_li_keep / n_c):

        E = 0.5 * ΣΣ_{i,j ∈ occ} G_LiLi[i,j]
            − f · Σ_{i ∈ occ} v_LiC[i]
            + f² · G_CC

    where f = n_li_keep / n_c, G_LiLi is the (N_Li × N_Li) Li-Li geometric
    factor matrix, v_LiC[i] is the sum of Li-C geometric factors for Li site i,
    and G_CC is the constant C-C energy (same for all configs at the same SOC).

    Notes
    -----
    Absolute energies differ slightly from ``compute_ewald_energy`` when eta
    (auto-determined per structure) would change with n_li_keep.  Relative
    ordering *within* a fixed SOC level is preserved.
    """

    def __init__(
        self,
        lattice,
        li_frac: np.ndarray,
        li_species: list,
        c_frac: np.ndarray,
        c_species: list,
        acc_factor: float = 12.0,
    ):
        n_li = len(li_frac)
        n_c = len(c_frac)
        self.n_li_total = n_li
        self.n_c = n_c

        if n_li == 0 or n_c == 0:
            self.G_LiLi = np.zeros((n_li, n_li))
            self.v_LiC = np.zeros(n_li)
            self.G_CC = 0.0
            return

        # Build reference structure: all Li + C sites with unit charge (+1).
        # This yields the pure geometric Ewald interaction factors independent
        # of the actual charge assignment (which varies per SOC level).
        all_frac = np.vstack([li_frac, c_frac])
        all_species_raw = list(li_species) + list(c_species)
        ref_struct = Structure(lattice, all_species_raw, all_frac.tolist())
        ref_struct.add_oxidation_state_by_site([1.0] * (n_li + n_c))

        ewald = EwaldSummation(ref_struct, acc_factor=acc_factor)
        M = ewald.total_energy_matrix  # (n_li + n_c, n_li + n_c)
        # total_energy = 0.5 * M.sum()  where M[i,j] = q_i*q_j*G(r_ij)
        # With unit charges: M[i,j] = G(r_ij) (pure geometric factor).

        # Li-Li block: indices 0 … n_li-1
        self.G_LiLi = np.asarray(M[:n_li, :n_li])       # (n_li, n_li)
        # v_LiC[i] = Σ_j G(r_{Li_i, C_j})  (row sums of the Li-C off-diag block)
        self.v_LiC = np.asarray(M[:n_li, n_li:]).sum(axis=1)  # (n_li,)
        # C-C self-energy (constant per stacking / config at same SOC)
        self.G_CC = float(0.5 * np.asarray(M[n_li:, n_li:]).sum())

    def energy(self, occupied: Tuple[int, ...], n_li_keep: int) -> float:
        """
        Compute Ewald energy for a Li configuration.

        Parameters
        ----------
        occupied : tuple of int
            Indices (into li_frac) of occupied Li sites.
        n_li_keep : int
            Number of occupied Li sites; must equal len(occupied).

        Formula derivation
        ------------------
        pymatgen total_energy_matrix M satisfies:
            total_energy = sum(M)   (NOT 0.5*sum)
        where M[i,j] = 0.5*q_i*q_j*φ(r_ij) for i≠j  and  M[i,i] = q_i²*φ_self.
        With unit charges q=1, M encodes the pure geometric factors G_unit.

        For charges Li=+1, C=-f (f = n_li_keep/n_c):
          E = Σ_{i,j ∈ occ} A_LiLi[i,j]          ← Li-Li (incl. self + both directions of each pair)
            − 2f · Σ_{i ∈ occ} v_LiC[i]           ← Li-C cross (both directions counted)
            + 2f² · G_CC                            ← C-C (= f² · M_CC.sum())
        where G_CC = 0.5 * M_CC.sum() is stored for the ½-sum; hence factor 2 here.
        """
        if n_li_keep == 0:
            return 0.0
        f = n_li_keep / self.n_c
        occ = np.asarray(occupied, dtype=int)
        # Li-Li: Σ_{i,j ∈ occ} G_LiLi[i,j]  (no extra ½; M already stores ½ of pair energy
        # in each of M[i,j] and M[j,i], so summing both gives the full pair contribution)
        e_lili = self.G_LiLi[np.ix_(occ, occ)].sum()
        # Li-C: both directions A[Li,C] + A[C,Li] = 2*(+1)(-f)*G_unit[Li,C]
        e_lic = -2.0 * f * self.v_LiC[occ].sum()
        # C-C: f² * M_CC.sum() = 2*f² * G_CC  (G_CC = 0.5 * M_CC.sum())
        e_cc = 2.0 * f * f * self.G_CC
        return float(e_lili + e_lic + e_cc)


