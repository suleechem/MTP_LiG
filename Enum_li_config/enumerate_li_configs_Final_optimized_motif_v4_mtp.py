def _check_site_separation(li_frac: np.ndarray, match_tol: float) -> None:
    n = len(li_frac)
    if n < 2:
        logger.info("Min Li-Li fractional separation: N/A (n_Li < 2)")
        return
    # Vectorised pairwise minimum-image distance in fractional coordinates.
    diff = li_frac[:, None, :] - li_frac[None, :, :]   # (n, n, 3)
    diff = diff - np.round(diff)                         # minimum image
    dists = np.linalg.norm(diff, axis=-1)               # (n, n)
    np.fill_diagonal(dists, np.inf)
    min_sep = float(np.min(dists))
    if min_sep < 10 * match_tol:
        logger.warning(
            f"Min Li-Li fractional separation {min_sep:.4f} is only "
            f"{min_sep / match_tol:.1f}× match_tol={match_tol}. "
            "Consider reducing --match-tol."
        )
    else:
        logger.info(
            f"Min Li-Li fractional separation: {min_sep:.4f} "
            f"(safety factor {min_sep / match_tol:.0f}×)"
        )


def get_li_site_permutations(
    struct: Structure,
    li_frac: np.ndarray,
    symprec: float = 0.01,
    match_tol: float = 0.01,
) -> List[Tuple[int, ...]]:
    sga = SpacegroupAnalyzer(struct, symprec=symprec)
    sg_symbol = sga.get_space_group_symbol()
    sg_number = sga.get_space_group_number()
    symmops = sga.get_symmetry_operations()
    logger.info(
        f"Space group: {sg_symbol} (#{sg_number})  |  "
        f"{len(symmops)} symmetry operations"
    )
    _check_site_separation(li_frac, match_tol)

    n_li = len(li_frac)
    raw_perms: List[Tuple[int, ...]] = []

    for op_idx, op in enumerate(symmops):
        new_coords = op.operate_multi(li_frac)   # (n_li, 3)
        # Vectorised matching: compute all pairwise minimum-image distances at once.
        # diff[i, j] = new_coords[i] − li_frac[j]  (minimum image)
        diff = new_coords[:, None, :] - li_frac[None, :, :]  # (n_li, n_li, 3)
        diff = diff - np.round(diff)
        dists = np.linalg.norm(diff, axis=-1)                 # (n_li, n_li)
        matches = np.argmin(dists, axis=1)                    # best match per row
        min_dists = dists[np.arange(n_li), matches]
        bad = np.where(min_dists >= match_tol)[0]
        if len(bad):
            bad_i = int(bad[0])
            raise RuntimeError(
                f"Symmetry op #{op_idx} maps Li site {bad_i} → {new_coords[bad_i]}, "
                f"no match within tol={match_tol}. "
                "Try --symprec 0.05 or --match-tol 0.02."
            )
        perm = tuple(int(j) for j in matches)
        if len(set(perm)) != n_li:
            raise RuntimeError(
                f"Symmetry op #{op_idx} is not a bijection: {list(perm)}"
            )
        raw_perms.append(perm)

    unique_perms = list(dict.fromkeys(raw_perms))
    identity = tuple(range(n_li))
    if identity not in unique_perms:
        unique_perms.insert(0, identity)

    logger.info(
        f"{len(raw_perms)} raw ops → {len(unique_perms)} unique Li-site permutations"
    )
    return unique_perms


