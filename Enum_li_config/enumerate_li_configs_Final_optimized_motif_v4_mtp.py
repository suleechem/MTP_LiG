def build_structure(
    lattice,
    occupied: Tuple[int, ...],
    li_frac: np.ndarray,
    li_species: list,
    c_frac: np.ndarray,
    c_species: list,
) -> Structure:
    if len(occupied) == 0:
        return Structure(lattice, list(c_species), c_frac.tolist())
    species = [li_species[i] for i in occupied] + list(c_species)
    coords = [li_frac[i].tolist() for i in occupied] + c_frac.tolist()
    return Structure(lattice, species, coords)



