MTP_TYPE_BY_ELEMENT = {"Li": 0, "C": 1}
MTP_ELEMENT_BY_TYPE = {0: "Li", 1: "C"}


def write_mtp_cfg_structure(
    struct: Structure,
    file_handle,
    feature: Optional[str] = None,
    type_by_element: Optional[Dict[str, int]] = None,
) -> None:
    """Write one pymatgen Structure in MLIP CFG format.

    The atom-type convention follows the uploaded PSO scripts: Li -> 0, C -> 1.
    Atoms are written with Li first, then non-Li atoms, to match the PSO MTP
    helper and to make relaxed output easier to compare with generated input.
    """
    type_by_element = type_by_element or MTP_TYPE_BY_ELEMENT
    sites = list(struct.sites)
    sites_sorted = [site for site in sites if site.specie.symbol == "Li"] + [
        site for site in sites if site.specie.symbol != "Li"
    ]

    file_handle.write("BEGIN_CFG\n")
    file_handle.write(f"Size\n  {len(sites_sorted)}\n")
    file_handle.write("Supercell\n")
    for row in struct.lattice.matrix:
        file_handle.write("  {:.10f} {:.10f} {:.10f}\n".format(*row))
    file_handle.write("AtomData:  id type       cartes_x      cartes_y      cartes_z\n")
    for new_id, site in enumerate(sites_sorted, start=1):
        elem = site.specie.symbol
        if elem not in type_by_element:
            raise ValueError(
                f"No MTP atom type is defined for element '{elem}'. "
                "Extend MTP_TYPE_BY_ELEMENT if your structure contains other species."
            )
        x, y, z = site.coords
        file_handle.write(
            f"{new_id:>8} {type_by_element[elem]:>4} {x:14.8f} {y:14.8f} {z:14.8f}\n"
        )
    if feature is not None:
        file_handle.write(f"Feature   {feature}\n")
    else:
        file_handle.write("Feature   Structure\n")
    file_handle.write("END_CFG\n")


def split_cfg_blocks_from_text(text: str) -> List[str]:
    blocks: List[str] = []
    current: List[str] = []
    inside = False
    for line in text.splitlines(True):
        if "BEGIN_CFG" in line:
            inside = True
            current = [line]
        elif "END_CFG" in line and inside:
            current.append(line)
            blocks.append("".join(current))
            inside = False
            current = []
        elif inside:
            current.append(line)
    return blocks


def split_cfg_blocks_file(path: Path) -> List[str]:
    if not path.exists() or path.stat().st_size == 0:
        return []
    return split_cfg_blocks_from_text(path.read_text(errors="replace"))


def parse_mtp_cfg_block(block_str: str) -> Tuple[Structure, Optional[float]]:
    """Parse a single MLIP CFG block into a pymatgen Structure and energy."""
    lines = block_str.strip().splitlines()
    cell: List[List[float]] = []
    positions: List[List[float]] = []
    species: List[str] = []
    energy: Optional[float] = None
    n_atoms: Optional[int] = None
    i = 0
    while i < len(lines):
        line = lines[i].strip()
        if line == "Size" and i + 1 < len(lines):
            n_atoms = int(lines[i + 1].strip())
            i += 2
            continue
        if line == "Supercell":
            cell = []
            for j in range(3):
                cell.append([float(x) for x in lines[i + 1 + j].split()[:3]])
            i += 4
            continue
        if line.startswith("AtomData"):
            if n_atoms is None:
                raise ValueError("CFG AtomData found before Size.")
            positions = []
            species = []
            for j in range(n_atoms):
                toks = lines[i + 1 + j].split()
                if len(toks) < 5:
                    raise ValueError(f"Malformed AtomData line: {lines[i + 1 + j]}")
                atom_type = int(float(toks[1]))
                species.append(MTP_ELEMENT_BY_TYPE.get(atom_type, "X"))
                positions.append([float(toks[2]), float(toks[3]), float(toks[4])])
            i += 1 + n_atoms
            continue
        if line == "Energy" and i + 1 < len(lines):
            try:
                energy = float(lines[i + 1].strip().split()[0])
            except Exception:
                energy = None
            i += 2
            continue
        i += 1
    if not cell or not positions:
        raise ValueError("Failed to parse Supercell/AtomData from CFG block.")
    struct = Structure(Lattice(np.array(cell, dtype=float)), species, positions, coords_are_cartesian=True)
    return struct, energy


def _candidate_feature(candidate: Tuple[str, Tuple[int, ...]], index: int, generation: Optional[int]) -> str:
    stacking, occupied = candidate
    gen_txt = "full" if generation is None else f"gen{generation:03d}"
    sites_txt = "-".join(str(i) for i in occupied)
    return f"Structure_{index} {gen_txt} stacking={stacking} sites={sites_txt}"


class MTPRelaxationEvaluator:
    """Batched MLIP relaxation backend for enumeration and GA scoring.

    It writes generated candidates to IN_*.cfg files, runs ``mlp relax`` on
    chunked multi-CFG inputs, parses relaxed energies, and keeps a cache keyed
    by (stacking_sequence, occupied_Li_sites).
    """

    def __init__(
        self,
        mlp_bin: str,
        mlip_ini: str,
        output_root: Path,
        failed_energy: float = 1.0e100,
        force_tolerance: Optional[float] = None,
        workers: int = 1,
        batch_size: int = 0,
        keep_workdirs: bool = False,
        extra_args: Optional[List[str]] = None,
    ):
        self.mlp_bin = mlp_bin
        self.mlip_ini = str(Path(mlip_ini).resolve()) if mlip_ini else "mlip.ini"
        self.output_root = Path(output_root)
        self.failed_energy = float(failed_energy)
        self.force_tolerance = force_tolerance
        self.workers = max(1, int(workers or 1))
        self.batch_size = int(batch_size or 0)
        self.keep_workdirs = bool(keep_workdirs)
        self.extra_args = extra_args or []
        self.energy_cache: Dict[Tuple[str, Tuple[int, ...]], float] = {}
        self.relaxed_structure_cache: Dict[Tuple[str, Tuple[int, ...]], Structure] = {}
        self.relaxed_block_cache: Dict[Tuple[str, Tuple[int, ...]], str] = {}

    def build_candidate_structure(
        self,
        lattice,
        candidate: Tuple[str, Tuple[int, ...]],
        li_frac: np.ndarray,
        li_species: list,
        c_frac_by_stacking: Dict[str, np.ndarray],
        c_species: list,
    ) -> Structure:
        stacking, occupied = candidate
        return build_structure(
            lattice, occupied, li_frac, li_species,
            c_frac_by_stacking[stacking], c_species,
        )

    def write_cfg(
        self,
        path: Path,
        candidates: List[Tuple[str, Tuple[int, ...]]],
        structures: List[Structure],
        generation: Optional[int] = None,
        start_index: int = 0,
    ) -> None:
        path.parent.mkdir(parents=True, exist_ok=True)
        with open(path, "w") as f:
            for local_i, (candidate, struct) in enumerate(zip(candidates, structures)):
                write_mtp_cfg_structure(
                    struct, f,
                    feature=_candidate_feature(candidate, start_index + local_i, generation),
                )
                f.write("\n")

    def _collect_relaxed_blocks(self, out_prefix: Path) -> List[str]:
        paths: List[Path] = []
        if out_prefix.exists():
            paths.append(out_prefix)
        paths.extend(sorted(out_prefix.parent.glob(out_prefix.name + "_*")))
        # Some mlp versions write a single file with suffix _0; others can write
        # one block per suffix.  Sort numeric suffixes naturally when possible.
        def suffix_key(path: Path):
            name = path.name
            tail = name.replace(out_prefix.name, "", 1).lstrip("_")
            try:
                return (0, int(tail))
            except Exception:
                return (1, tail)
        blocks: List[str] = []
        for path in sorted(dict.fromkeys(paths), key=suffix_key):
            blocks.extend(split_cfg_blocks_file(path))
        return blocks

    def _run_relax_chunk(self, in_path: Path, out_prefix: Path, log_path: Path) -> Tuple[int, List[str]]:
        cmd = [self.mlp_bin, "relax", self.mlip_ini, f"--cfg-filename={in_path}", f"--save-relaxed={out_prefix}"]
        if self.force_tolerance is not None:
            cmd.append(f"--force-tolerance={self.force_tolerance}")
        cmd.extend(self.extra_args)
        log_path.parent.mkdir(parents=True, exist_ok=True)
        with open(log_path, "w") as lf:
            proc = subprocess.run(cmd, stdout=lf, stderr=lf, check=False)
        blocks = self._collect_relaxed_blocks(out_prefix)
        return proc.returncode, blocks

    def evaluate_candidates(
        self,
        candidates: List[Tuple[str, Tuple[int, ...]]],
        lattice,
        li_frac: np.ndarray,
        li_species: list,
        c_frac_by_stacking: Dict[str, np.ndarray],
        c_species: list,
        soc_dir: Path,
        label: str,
        generation: Optional[int] = None,
        aggregate_input_name: Optional[str] = None,
        to_relax_name: Optional[str] = None,
    ) -> List[float]:
        if not candidates:
            return []

        soc_dir = Path(soc_dir)
        soc_dir.mkdir(parents=True, exist_ok=True)
        cfg_dir = soc_dir / "mtp_cfg"
        log_dir = soc_dir / "mtp_logs"
        work_dir = soc_dir / "mtp_work" / label
        cfg_dir.mkdir(parents=True, exist_ok=True)
        log_dir.mkdir(parents=True, exist_ok=True)
        work_dir.mkdir(parents=True, exist_ok=True)

        # Always write the generation/SOC input file requested by the user.
        all_structures = [
            self.build_candidate_structure(lattice, c, li_frac, li_species, c_frac_by_stacking, c_species)
            for c in candidates
        ]
        if aggregate_input_name is None:
            aggregate_input_name = f"IN_{label}.cfg"
        aggregate_path = cfg_dir / aggregate_input_name
        self.write_cfg(aggregate_path, candidates, all_structures, generation=generation)

        # Only relax candidates that have not been evaluated before.
        missing: List[Tuple[str, Tuple[int, ...]]] = []
        missing_structures: List[Structure] = []
        for candidate, struct in zip(candidates, all_structures):
            if candidate not in self.energy_cache:
                missing.append(candidate)
                missing_structures.append(struct)

        if missing:
            if self.batch_size and self.batch_size > 0:
                chunks = [
                    (missing[i:i + self.batch_size], missing_structures[i:i + self.batch_size], i)
                    for i in range(0, len(missing), self.batch_size)
                ]
            else:
                chunks = [(missing, missing_structures, 0)]

            # For the non-GA full enumeration path, also create to-relax.cfg as
            # the exact MLP input name specified by the user.
            if to_relax_name is not None and len(chunks) == 1:
                self.write_cfg(soc_dir / to_relax_name, missing, missing_structures, generation=generation)

            def submit_chunk(chunk_id: int, chunk_candidates, chunk_structures, start_idx: int):
                chunk_in = work_dir / f"to-relax_{chunk_id:04d}.cfg"
                chunk_out = work_dir / f"relaxed_{chunk_id:04d}.cfg"
                chunk_log = log_dir / f"relax_{label}_chunk{chunk_id:04d}.txt"
                self.write_cfg(chunk_in, chunk_candidates, chunk_structures, generation=generation, start_index=start_idx)
                rc, blocks = self._run_relax_chunk(chunk_in, chunk_out, chunk_log)
                return chunk_id, rc, chunk_candidates, blocks

            results = []
            if self.workers > 1 and len(chunks) > 1:
                with ThreadPoolExecutor(max_workers=self.workers) as ex:
                    futs = [
                        ex.submit(submit_chunk, ci, cc, ss, start)
                        for ci, (cc, ss, start) in enumerate(chunks)
                    ]
                    for fut in as_completed(futs):
                        results.append(fut.result())
            else:
                for ci, (cc, ss, start) in enumerate(chunks):
                    results.append(submit_chunk(ci, cc, ss, start))
            results.sort(key=lambda x: x[0])

            # Combine relaxed blocks for easy inspection.
            relaxed_combined = cfg_dir / f"relaxed_{label}.cfg"
            with open(relaxed_combined, "w") as fout:
                for _, _, _, blocks in results:
                    for block in blocks:
                        fout.write(block)
                        if not block.endswith("\n"):
                            fout.write("\n")
                        fout.write("\n")

            for _, rc, chunk_candidates, blocks in results:
                if rc != 0:
                    logger.warning(f"MTP relax returned non-zero exit code {rc} for label={label}.")
                if len(blocks) < len(chunk_candidates):
                    logger.warning(
                        f"MTP relax produced {len(blocks)} CFG blocks for {len(chunk_candidates)} inputs "
                        f"at label={label}; missing candidates receive penalty energy."
                    )
                for local_i, candidate in enumerate(chunk_candidates):
                    if local_i >= len(blocks):
                        self.energy_cache[candidate] = self.failed_energy
                        continue
                    block = blocks[local_i]
                    try:
                        relaxed_struct, energy = parse_mtp_cfg_block(block)
                    except Exception as exc:
                        logger.warning(f"Failed to parse relaxed CFG for {candidate}: {exc}")
                        self.energy_cache[candidate] = self.failed_energy
                        continue
                    if energy is None:
                        logger.warning(f"No Energy field found in relaxed CFG for {candidate}; using penalty energy.")
                        self.energy_cache[candidate] = self.failed_energy
                    else:
                        self.energy_cache[candidate] = float(energy)
                    self.relaxed_structure_cache[candidate] = relaxed_struct
                    self.relaxed_block_cache[candidate] = block

            if not self.keep_workdirs:
                try:
                    shutil.rmtree(work_dir)
                except Exception:
                    pass

        return [self.energy_cache.get(c, self.failed_energy) for c in candidates]

def compute_ewald_energy(struct: Structure, n_li: int, n_c: int) -> float:
    """
    Ewald electrostatic energy (eV). Assigns Li=+1, C=−n_li/n_c for charge
    neutrality. Returns 0.0 for n_li=0.

    Within a fixed SOC level the C-C term is constant, so energy differences
    purely reflect Li arrangement.
    """
    if n_li == 0:
        return 0.0
    struct_oxi = struct.copy()
    struct_oxi.add_oxidation_state_by_element({"Li": 1.0, "C": -n_li / n_c})
    return EwaldElectrostaticModel().get_energy(struct_oxi)


