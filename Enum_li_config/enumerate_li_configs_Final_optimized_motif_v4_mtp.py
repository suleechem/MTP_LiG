def _rounded_frac_tuple(frac: np.ndarray, decimals: int = 8) -> Tuple[float, float, float]:
    """Rounded fractional-coordinate tuple for structure-duplicate keys."""
    x = np.mod(np.array(frac, dtype=float), 1.0)
    # Map values very close to 1 back to 0 after rounding.
    x = np.round(x, decimals=decimals)
    x[np.isclose(x, 1.0, atol=10 ** (-decimals))] = 0.0
    return (float(x[0]), float(x[1]), float(x[2]))


def structure_duplicate_key(
    occupied: Tuple[int, ...],
    li_frac: np.ndarray,
    c_frac: np.ndarray,
    decimals: int = 8,
) -> Tuple[Tuple[Tuple[float, float, float], ...], Tuple[Tuple[float, float, float], ...]]:
    """
    Coordinate-based key for removing exactly duplicated generated structures.

    The key deliberately does not include a stacking label or Li-site indices.
    Two candidates are considered duplicates if they generate the same set of
    Li fractional coordinates and the same set of C fractional coordinates
    within the rounding tolerance. This removes duplicates introduced by
    hollow-site expansion, periodic wrapping, repeated stacking labels, or
    equivalent candidate bookkeeping.
    """
    li_key = tuple(sorted(_rounded_frac_tuple(li_frac[i], decimals) for i in occupied))
    c_key = tuple(sorted(_rounded_frac_tuple(x, decimals) for x in c_frac))
    return li_key, c_key


def build_stacking_candidate_structure(
    lattice,
    candidate: Tuple[str, Tuple[int, ...]],
    li_frac: np.ndarray,
    li_species: list,
    c_frac_by_stacking: Dict[str, np.ndarray],
    c_species: list,
) -> Structure:
    """Build a Structure for a stacking-aware candidate."""
    stacking, occupied = candidate
    return build_structure(
        lattice,
        occupied,
        li_frac,
        li_species,
        c_frac_by_stacking[stacking],
        c_species,
    )


def make_structure_matcher(
    ltol: float = 0.2,
    stol: float = 0.3,
    angle_tol: float = 5.0,
) -> StructureMatcher:
    """
    StructureMatcher for symmetry-equivalence duplicate filtering.

    primitive_cell=False and attempt_supercell=False keep the generated
    supercell size fixed. scale=False prevents volume rescaling. Within that
    fixed cell, StructureMatcher can still identify equivalent structures
    related by origin shifts, periodic translations, rotations/reflections, and
    site permutations consistent with the lattice.
    """
    return StructureMatcher(
        ltol=ltol,
        stol=stol,
        angle_tol=angle_tol,
        primitive_cell=False,
        scale=False,
        attempt_supercell=False,
        comparator=ElementComparator(),
    )


def dedupe_stacking_candidates_by_structure(
    candidates: List[Tuple[str, Tuple[int, ...]]],
    energies: List[float],
    lattice,
    li_frac: np.ndarray,
    li_species: list,
    c_frac_by_stacking: Dict[str, np.ndarray],
    c_species: list,
    mode: str = "symmetry",
    decimals: int = 8,
    matcher: Optional[StructureMatcher] = None,
) -> Tuple[List[Tuple[str, Tuple[int, ...]]], List[float], int]:
    """
    Remove duplicate generated structures from a SOC candidate list.

    mode='exact'
        Removes only candidates with identical fractional-coordinate sets.

    mode='symmetry'
        First removes exact coordinate duplicates, then uses pymatgen
        StructureMatcher to remove structures equivalent by periodic
        translation, origin shift, rotation/reflection, or other symmetry-like
        operations of the fixed supercell. Candidates are processed in energy
        order, so the lowest-energy representative is retained.

    mode='none'
        Returns all candidates sorted by energy.
    """
    if len(candidates) != len(energies):
        raise ValueError("candidates and energies must have the same length")

    n_in = len(candidates)
    if n_in == 0:
        return [], [], 0

    sorted_input = sorted(zip(energies, candidates), key=lambda x: x[0])
    if mode == "none":
        return [c for _, c in sorted_input], [e for e, _ in sorted_input], 0

    # Fast exact-coordinate pass. This removes bookkeeping duplicates caused by
    # hollow-site expansion, repeated stacking labels that generate identical
    # coordinates, and periodic wrapping.
    best_by_key: Dict[
        Tuple[Tuple[Tuple[float, float, float], ...], Tuple[Tuple[float, float, float], ...]],
        Tuple[float, Tuple[str, Tuple[int, ...]]],
    ] = {}
    for energy, candidate in sorted_input:
        stacking, occupied = candidate
        key = structure_duplicate_key(
            occupied,
            li_frac,
            c_frac_by_stacking[stacking],
            decimals=decimals,
        )
        if key not in best_by_key:
            best_by_key[key] = (energy, candidate)

    exact_unique = sorted(best_by_key.values(), key=lambda x: x[0])
    if mode == "exact":
        return [c for _, c in exact_unique], [e for e, _ in exact_unique], n_in - len(exact_unique)

    if mode != "symmetry":
        raise ValueError(f"Unsupported duplicate-filtering mode: {mode}")

    if matcher is None:
        matcher = make_structure_matcher()

    unique_items: List[Tuple[float, Tuple[str, Tuple[int, ...]], Structure]] = []
    for energy, candidate in exact_unique:
        struct = build_stacking_candidate_structure(
            lattice,
            candidate,
            li_frac,
            li_species,
            c_frac_by_stacking,
            c_species,
        )
        is_duplicate = False
        for _, _, ref_struct in unique_items:
            if matcher.fit(struct, ref_struct):
                is_duplicate = True
                break
        if not is_duplicate:
            unique_items.append((energy, candidate, struct))

    deduped_candidates = [candidate for energy, candidate, struct in unique_items]
    deduped_energies = [energy for energy, candidate, struct in unique_items]
    return deduped_candidates, deduped_energies, n_in - len(deduped_candidates)


def select_top_symmetry_unique_candidates(
    candidates: List[Tuple[str, Tuple[int, ...]]],
    energies: List[float],
    lattice,
    li_frac: np.ndarray,
    li_species: list,
    c_frac_by_stacking: Dict[str, np.ndarray],
    c_species: list,
    top_n: int = MAX_CIF_PER_SOC,
    mode: str = "symmetry",
    decimals: int = 8,
    matcher: Optional[StructureMatcher] = None,
) -> Tuple[List[Tuple[str, Tuple[int, ...]]], List[float], dict]:
    """
    Select the lowest-energy top_n structures after duplicate filtering.

    This is intentionally optimized for the final CIF-selection step:
      1. Sort all evaluated/generated candidates by Ewald energy.
      2. Remove exact coordinate duplicates with a fast fractional-coordinate hash.
      3. If mode='symmetry', scan candidates in energy order and use
         StructureMatcher only until top_n symmetry-unique structures are found.

    The function does not necessarily enumerate every symmetry-equivalence
    class in the full candidate set; it finds the requested low-energy unique
    representative set efficiently.
    """
    if len(candidates) != len(energies):
        raise ValueError("candidates and energies must have the same length")

    n_input = len(candidates)
    stats = {
        "n_candidates_input": int(n_input),
        "n_exact_duplicates_removed": 0,
        "n_symmetry_duplicates_skipped_for_top20": 0,
        "n_symmetry_unique_cifs_written": 0,
        "dedup_mode": mode,
    }
    if n_input == 0:
        return [], [], stats

    sorted_input = sorted(zip(energies, candidates), key=lambda x: x[0])
    if mode == "none":
        selected = sorted_input[:top_n]
        stats["n_symmetry_unique_cifs_written"] = len(selected)
        return [c for _, c in selected], [e for e, _ in selected], stats

    # Fast exact-coordinate pass. Since candidates are already energy-sorted,
    # the first occurrence of a coordinate key is the lowest-energy representative.
    exact_unique: List[Tuple[float, Tuple[str, Tuple[int, ...]]]] = []
    seen_exact_keys = set()
    for energy, candidate in sorted_input:
        stacking, occupied = candidate
        key = structure_duplicate_key(
            occupied,
            li_frac,
            c_frac_by_stacking[stacking],
            decimals=decimals,
        )
        if key in seen_exact_keys:
            stats["n_exact_duplicates_removed"] += 1
            continue
        seen_exact_keys.add(key)
        exact_unique.append((energy, candidate))

    if mode == "exact":
        selected = exact_unique[:top_n]
        stats["n_symmetry_unique_cifs_written"] = len(selected)
        return [c for _, c in selected], [e for e, _ in selected], stats

    if mode != "symmetry":
        raise ValueError(f"Unsupported duplicate-filtering mode: {mode}")

    if matcher is None:
        matcher = make_structure_matcher()

    selected_items: List[Tuple[float, Tuple[str, Tuple[int, ...]], Structure]] = []
    for energy, candidate in exact_unique:
        struct = build_stacking_candidate_structure(
            lattice,
            candidate,
            li_frac,
            li_species,
            c_frac_by_stacking,
            c_species,
        )
        is_duplicate = False
        for _, _, ref_struct in selected_items:
            if matcher.fit(struct, ref_struct):
                is_duplicate = True
                break
        if is_duplicate:
            stats["n_symmetry_duplicates_skipped_for_top20"] += 1
            continue
        selected_items.append((energy, candidate, struct))
        if len(selected_items) >= top_n:
            break

    stats["n_symmetry_unique_cifs_written"] = len(selected_items)
    selected_candidates = [candidate for energy, candidate, struct in selected_items]
    selected_energies = [energy for energy, candidate, struct in selected_items]
    return selected_candidates, selected_energies, stats


