def write_soc_top_cifs(
    n_li_keep: int,
    n_li_total: int,
    reps: List[Tuple[int, ...]],
    energies: List[float],
    parent: Structure,
    li_frac: np.ndarray,
    li_species: list,
    c_frac: np.ndarray,
    c_species: list,
    out_dir: Path,
    top_n: int,
) -> List[dict]:
    soc = n_li_keep / n_li_total
    n_c = len(c_frac)
    cif_dir = out_dir / f"SOC_{soc:.4f}_nLi{n_li_keep:02d}" / "top_stable_cif"
    cif_dir.mkdir(parents=True, exist_ok=True)

    sorted_pairs = sorted(zip(energies, reps), key=lambda x: x[0])

    records = []
    for rank, (energy, occupied) in enumerate(sorted_pairs[:top_n], start=1):
        n_atoms = n_li_keep + n_c
        e_per_atom = energy / n_atoms if n_atoms else 0.0
        struct = build_structure(
            parent.lattice, occupied, li_frac, li_species, c_frac, c_species
        )
        fname = (
            f"rank{rank:03d}_nLi{n_li_keep:02d}"
            f"_SOC{soc:.4f}_E{energy:.3f}eV.cif"
        )
        CifWriter(struct).write_file(str(cif_dir / fname))
        records.append({
            "rank": rank,
            "occupied_Li_sites": list(occupied),
            "ewald_energy_eV": round(energy, 6),
            "ewald_energy_per_atom_eV": round(e_per_atom, 8),
            "cif_file": str(cif_dir / fname),
        })
    return records


