def load_and_build_supercell(
    filepath: str, supercell_matrix: List[int]
) -> Tuple[Structure, int, int]:
    path = Path(filepath)
    if not path.exists():
        raise FileNotFoundError(f"Structure file not found: {filepath}")

    logger.info(f"Loading unit cell: {filepath}")
    unit_cell = Structure.from_file(str(path))
    comp_uc = unit_cell.composition
    n_li_uc = int(comp_uc["Li"]) if "Li" in comp_uc else 0
    n_c_uc = int(comp_uc["C"]) if "C" in comp_uc else 0
    logger.info(
        f"Unit cell: {n_li_uc} Li + {n_c_uc} C  |  "
        f"a={unit_cell.lattice.a:.4f} Å  b={unit_cell.lattice.b:.4f} Å  "
        f"c={unit_cell.lattice.c:.4f} Å"
    )

    supercell = unit_cell.copy()
    supercell.make_supercell(supercell_matrix)

    comp_sc = supercell.composition
    n_li = int(comp_sc["Li"]) if "Li" in comp_sc else 0
    n_c = int(comp_sc["C"]) if "C" in comp_sc else 0

    if n_li == 0:
        raise ValueError("No Li atoms found. Check element symbols in structure file.")
    if n_c == 0:
        raise ValueError("No C atoms found. Check element symbols in structure file.")

    sc_label = "×".join(str(x) for x in supercell_matrix)
    logger.info(
        f"Supercell ({sc_label}): {n_li} Li + {n_c} C = {n_li + n_c} atoms  |  "
        f"a={supercell.lattice.a:.4f} Å  b={supercell.lattice.b:.4f} Å  "
        f"c={supercell.lattice.c:.4f} Å"
    )
    return supercell, n_li, n_c


def extract_sublattices(struct: Structure):
    li_idx = [i for i, s in enumerate(struct) if s.specie.symbol == "Li"]
    c_idx = [i for i, s in enumerate(struct) if s.specie.symbol == "C"]
    li_frac = np.array([struct[i].frac_coords for i in li_idx])
    c_frac = np.array([struct[i].frac_coords for i in c_idx])
    li_species = [struct[i].species for i in li_idx]
    c_species = [struct[i].species for i in c_idx]
    return li_idx, c_idx, li_frac, c_frac, li_species, c_species


def detect_layers_by_z(frac_coords: np.ndarray, tol: float = 0.05) -> List[List[int]]:
    """Group site indices by fractional z-coordinate and sort layers along c."""
    zcoords = frac_coords[:, 2]
    layer_centers: List[float] = []
    layer_members: List[List[int]] = []

    for i, z in enumerate(zcoords):
        placed = False
        for k, zc in enumerate(layer_centers):
            dz = abs(z - zc)
            dz = min(dz, 1.0 - dz)  # PBC
            if dz < tol:
                layer_members[k].append(i)
                layer_centers[k] = float(np.mean([zcoords[j] for j in layer_members[k]]))
                placed = True
                break
        if not placed:
            layer_centers.append(float(z))
            layer_members.append([i])

    order = sorted(range(len(layer_members)), key=lambda k: layer_centers[k])
    return [layer_members[k] for k in order]


def detect_li_layers(li_frac: np.ndarray, tol: float = 0.05) -> List[List[int]]:
    """Group Li site indices by fractional z-coordinate (c-axis layers)."""
    return detect_layers_by_z(li_frac, tol=tol)


def generate_all_stacking_sequences(n_layers: int) -> List[str]:
    """
    Generate all A/B stacking sequences with the first layer fixed to A.

    Fixing the first layer to A removes the trivial global A↔B translation
    degeneracy. For four carbon layers, this returns sequences such as
    AAAA, ABAA, AABA, AAAB, ABAB, ...
    """
    if n_layers <= 0:
        return []
    if n_layers == 1:
        return ["A"]
    seqs: List[str] = []
    for bits in range(2 ** (n_layers - 1)):
        chars = ["A"]
        for j in range(n_layers - 1):
            chars.append("B" if (bits >> j) & 1 else "A")
        seqs.append("".join(chars))
    return seqs


def alternating_ab_sequence(n_layers: int) -> str:
    """Return ABAB... with length n_layers."""
    return "".join("A" if i % 2 == 0 else "B" for i in range(n_layers))


def validate_stacking_sequences(sequences: List[str], n_layers: int) -> List[str]:
    """Normalize, validate, and de-duplicate stacking sequences."""
    clean: List[str] = []
    for seq in sequences:
        seq_u = seq.upper().strip()
        if len(seq_u) != n_layers:
            raise ValueError(
                f"Invalid stacking sequence '{seq}'. Expected length {n_layers} "
                f"for {n_layers} detected carbon layers."
            )
        bad = sorted(set(seq_u) - {"A", "B"})
        if bad:
            raise ValueError(
                f"Invalid stacking sequence '{seq}'. Only A and B are allowed."
            )
        clean.append(seq_u)
    return list(dict.fromkeys(clean))


def get_aa_only_soc_threshold(policy: str, override: Optional[float]) -> Optional[float]:
    """Return the SOC threshold above which only all-A stacking is considered."""
    if policy == "none":
        return None
    if override is not None:
        if not (0.0 <= override <= 1.0):
            raise ValueError("--aa-only-soc-threshold must be between 0 and 1.")
        return float(override)
    if policy == "conservative":
        return 0.50
    if policy == "literature":
        return 0.40
    raise ValueError(f"Unsupported SOC stacking policy: {policy}")


def active_stacking_sequences_for_soc(
    soc: float,
    all_sequences: List[str],
    all_a_sequence: str,
    policy: str,
    aa_only_threshold: Optional[float],
) -> Tuple[List[str], dict]:
    """Return stacking sequences active at a given SOC under the selected policy.

    none
        Use all user-requested stacking sequences for every SOC.
    conservative
        Use all-A only for SOC >= 0.50.
    literature
        Use all-A only for SOC >= 0.40, reflecting the manuscript's
        high-SOC AA-dominant / AB-inaccessible regime.

    The all-A sequence is always available as the high-SOC fallback.
    """
    threshold = aa_only_threshold
    use_aa_only = threshold is not None and soc >= threshold
    if use_aa_only:
        active = [all_a_sequence]
        reason = f"SOC >= {threshold:.3f}: AA-only stacking constraint active"
    else:
        active = list(all_sequences)
        reason = "below AA-only SOC threshold; using requested stacking sequence set"
    return active, {
        "policy": policy,
        "aa_only_soc_threshold": threshold,
        "aa_only_active": bool(use_aa_only),
        "active_stacking_sequences": active,
        "reason": reason,
    }


def _cartesian_norm_for_frac2d(lattice, delta_ab: np.ndarray) -> float:
    """Return the Cartesian norm of an in-plane fractional displacement."""
    delta3 = np.array([delta_ab[0], delta_ab[1], 0.0], dtype=float)
    return float(np.linalg.norm(lattice.get_cartesian_coords(delta3)))


def auto_detect_b_layer_shift(
    lattice,
    li_frac: np.ndarray,
    c_frac_aa: np.ndarray,
    carbon_layers: List[List[int]],
) -> Tuple[Tuple[float, float], dict]:
    """
    Auto-detect the B-layer shift from the input AA structure.

    Definition used here:
      A stacking: graphene hexagon centers in adjacent carbon layers overlap.
      B stacking: a carbon vertex in the shifted layer lies above/below a
                  hexagon center of the reference A layer.

    In LiC6-like GIC input structures, Li sites are located near graphene
    hexagon centers. Therefore the reference hexagon centers are taken from
    Li-site xy projections. The B shift is chosen as the shortest in-plane
    translation from a carbon vertex in the first carbon layer to one of those
    Li-derived hexagon centers:

        shifted_vertex_xy = vertex_xy + b_shift_xy = hexagon_center_xy
    """
    if len(li_frac) == 0:
        raise ValueError(
            "Cannot auto-detect AB stacking shift because no Li sites were found. "
            "Provide --ab-shift DA DB manually."
        )
    if not carbon_layers:
        raise ValueError("Cannot auto-detect AB stacking shift because no carbon layers were detected.")

    reference_layer = carbon_layers[0]
    centers_xy = np.mod(li_frac[:, :2], 1.0)
    vertices_xy = np.mod(c_frac_aa[reference_layer, :2], 1.0)

    best = None
    for center_idx, center_xy in enumerate(centers_xy):
        for local_vertex_idx, vertex_xy in enumerate(vertices_xy):
            # Minimum-image in-plane vector that moves the carbon vertex onto
            # the hexagon center.
            delta = center_xy - vertex_xy
            delta = delta - np.round(delta)
            dist = _cartesian_norm_for_frac2d(lattice, delta)
            if best is None or dist < best[0]:
                best = (dist, delta, center_idx, local_vertex_idx, center_xy, vertex_xy)

    if best is None:
        raise ValueError("Failed to auto-detect AB stacking shift from Li/C coordinates.")

    dist, delta, center_idx, local_vertex_idx, center_xy, vertex_xy = best
    layer_vertex_index = reference_layer[local_vertex_idx]
    shift = (float(delta[0]), float(delta[1]))
    metadata = {
        "mode": "auto_ring_center_to_carbon_vertex",
        "definition": {
            "A": "hexagon centers overlap between carbon layers",
            "B": "a shifted-layer carbon vertex is placed above/below a reference-layer hexagon center",
        },
        "reference_carbon_layer_index": 0,
        "reference_li_site_index_for_hexagon_center": int(center_idx),
        "reference_carbon_site_index_for_vertex": int(layer_vertex_index),
        "hexagon_center_frac_ab": [float(center_xy[0]), float(center_xy[1])],
        "carbon_vertex_frac_ab_before_shift": [float(vertex_xy[0]), float(vertex_xy[1])],
        "b_shift_supercell_frac": [shift[0], shift[1]],
        "center_to_vertex_distance_angstrom": float(dist),
    }
    return shift, metadata


def make_carbon_stacking_fracs(
    c_frac_aa: np.ndarray,
    carbon_layers: List[List[int]],
    stacking_sequence: str,
    b_shift: Tuple[float, float],
) -> np.ndarray:
    """
    Return carbon fractional coordinates for a layer-resolved stacking sequence.

    Each character in stacking_sequence corresponds to one detected carbon layer
    sorted along c. A means the original AA-position layer is retained. B means
    that layer is shifted in-plane by b_shift. The default b_shift is detected
    so that a shifted-layer carbon vertex sits above/below a reference hexagon
    center, i.e. Bernal-like AB registry. Li fractional coordinates are unchanged.
    """
    seq = stacking_sequence.upper()
    if len(seq) != len(carbon_layers):
        raise ValueError(
            f"Stacking sequence '{stacking_sequence}' has length {len(seq)}, "
            f"but {len(carbon_layers)} carbon layers were detected."
        )
    shifted = np.array(c_frac_aa, copy=True)
    da, db = b_shift
    for layer_idx, members in enumerate(carbon_layers):
        marker = seq[layer_idx]
        if marker == "A":
            continue
        if marker != "B":
            raise ValueError(
                f"Unsupported stacking marker '{marker}' in sequence '{stacking_sequence}'. "
                "Only A and B are allowed."
            )
        shifted[members, 0] = (shifted[members, 0] + da) % 1.0
        shifted[members, 1] = (shifted[members, 1] + db) % 1.0

    return shifted


def _dedupe_frac_ab(
    lattice,
    points_ab: List[np.ndarray],
    tol_angstrom: float,
) -> List[np.ndarray]:
    """De-duplicate 2D fractional points using minimum-image in-plane distance.

    Uses vectorised numpy operations to check each candidate against the
    already-confirmed unique set, giving O(N) numpy calls instead of O(N²)
    pure-Python scalar loops.
    """
    if not points_ab:
        return []
    lat_mat = lattice.matrix            # (3, 3) row-vectors
    # Precompute a (2,2) in-plane sub-matrix for the Cartesian conversion.
    # cart_2d = frac_2d @ lat_mat[:2, :2]  (ignores z/c component)
    lat2 = lat_mat[:2, :2]             # (2, 2)

    pts = np.array([np.mod(np.array(p, dtype=float)[:2], 1.0)
                    for p in points_ab], dtype=float)  # (N, 2)

    unique_indices: List[int] = []
    unique_pts: List[np.ndarray] = []

    for i, p in enumerate(pts):
        if not unique_pts:
            unique_indices.append(i)
            unique_pts.append(p)
            continue
        prev = np.array(unique_pts)          # (k, 2)
        delta = p - prev                     # (k, 2)
        delta = delta - np.round(delta)      # minimum image
        cart_delta = delta @ lat2            # (k, 2)
        dists = np.linalg.norm(cart_delta, axis=1)  # (k,)
        if not np.any(dists < tol_angstrom):
            unique_indices.append(i)
            unique_pts.append(p)

    return [pts[i] for i in unique_indices]


def detect_graphene_hexagon_centers_from_carbon_layer(
    lattice,
    c_frac: np.ndarray,
    carbon_layer: List[int],
    cc_bond_cutoff: float = 1.85,
    dedupe_tol_angstrom: float = 0.35,
) -> List[np.ndarray]:
    """
    Detect graphene hexagon centers in one carbon layer by finding 6-membered
    C-C rings under 2D periodic boundary conditions.

    The returned coordinates are 2D fractional (a,b) positions in the generated
    supercell. They are used as possible Li hollow sites within every Li layer.
    """
    if len(carbon_layer) < 6:
        return []

    local_to_global = list(carbon_layer)
    n = len(local_to_global)
    coords_ab = np.array([c_frac[i, :2] for i in local_to_global], dtype=float)

    # Build a 2D periodic C-C bond graph.
    adj = {i: set() for i in range(n)}
    for i in range(n):
        for j in range(i + 1, n):
            delta = coords_ab[j] - coords_ab[i]
            delta = delta - np.round(delta)
            dist = _cartesian_norm_for_frac2d(lattice, delta)
            if 0.8 <= dist <= cc_bond_cutoff:
                adj[i].add(j)
                adj[j].add(i)

    cycles: set = set()

    def dfs(start: int, current: int, path: List[int]) -> None:
        if len(path) == 6:
            if start in adj[current]:
                cycles.add(tuple(sorted(path)))
            return
        for nb in adj[current]:
            if nb in path:
                continue
            # This simple ordering rule removes many duplicated traversals while
            # still allowing PBC-wrapping rings.
            if nb < start:
                continue
            dfs(start, nb, path + [nb])

    for start in range(n):
        dfs(start, start, [start])

    centers: List[np.ndarray] = []
    for cyc in cycles:
        cyc = list(cyc)
        base = coords_ab[cyc[0]]
        unwrapped = [base]
        for idx in cyc[1:]:
            delta = coords_ab[idx] - base
            delta = delta - np.round(delta)
            unwrapped.append(base + delta)
        pts = np.array(unwrapped)
        center = np.mod(np.mean(pts, axis=0), 1.0)

        # Reject non-face 6-cycles by checking the C-to-center radius.
        radii = []
        for idx in cyc:
            delta = coords_ab[idx] - center
            delta = delta - np.round(delta)
            radii.append(_cartesian_norm_for_frac2d(lattice, delta))
        if not radii:
            continue
        r_mean = float(np.mean(radii))
        r_std = float(np.std(radii))
        if 0.9 <= r_mean <= 1.8 and r_std <= 0.25:
            centers.append(center)

    return _dedupe_frac_ab(lattice, centers, dedupe_tol_angstrom)


def expand_li_sites_with_hollow_centers(
    lattice,
    li_frac: np.ndarray,
    li_species: list,
    c_frac: np.ndarray,
    carbon_layers: List[List[int]],
    li_layers: List[List[int]],
    cc_bond_cutoff: float = 1.85,
    dedupe_tol_angstrom: float = 0.35,
) -> Tuple[np.ndarray, list, List[List[int]], dict]:
    """
    Augment the GA Li candidate site pool with graphene hollow sites.

    Existing Li sites are treated as already occupied/defined hollow centers.
    Additional empty sites are generated from 6-membered carbon-ring centers in
    the reference carbon layer and replicated into each detected Li layer at the
    corresponding Li-layer z coordinate.
    """
    if len(li_frac) == 0 or not li_layers:
        return li_frac, li_species, li_layers, {
            "enabled": False,
            "reason": "no Li layer found",
            "n_input_li_sites": int(len(li_frac)),
            "n_ga_li_candidate_sites": int(len(li_frac)),
        }
    if not carbon_layers:
        return li_frac, li_species, li_layers, {
            "enabled": False,
            "reason": "no carbon layer found",
            "n_input_li_sites": int(len(li_frac)),
            "n_ga_li_candidate_sites": int(len(li_frac)),
        }

    reference_carbon_layer_index = 0
    hollow_xy = detect_graphene_hexagon_centers_from_carbon_layer(
        lattice,
        c_frac,
        carbon_layers[reference_carbon_layer_index],
        cc_bond_cutoff=cc_bond_cutoff,
        dedupe_tol_angstrom=dedupe_tol_angstrom,
    )

    # Preserve any Li positions from the input structure even if ring detection
    # misses a hollow site at a periodic boundary.
    merged_xy_raw = list(hollow_xy) + [np.mod(xy, 1.0) for xy in li_frac[:, :2]]
    merged_xy = _dedupe_frac_ab(lattice, merged_xy_raw, dedupe_tol_angstrom)
    n_hollow_xy_duplicates_removed = len(merged_xy_raw) - len(merged_xy)

    if not merged_xy:
        return li_frac, li_species, li_layers, {
            "enabled": False,
            "reason": "failed to detect hollow centers",
            "n_input_li_sites": int(len(li_frac)),
            "n_ga_li_candidate_sites": int(len(li_frac)),
        }

    species0 = li_species[0] if li_species else "Li"
    new_frac: List[List[float]] = []
    new_species: list = []
    new_layers: List[List[int]] = []

    for layer_members in li_layers:
        z_vals = [li_frac[i, 2] for i in layer_members]
        # Use circular mean for z to be safe near the periodic boundary.
        angles = np.array(z_vals) * 2 * np.pi
        z_center = (np.angle(np.mean(np.exp(1j * angles))) / (2 * np.pi)) % 1.0
        layer_indices: List[int] = []
        for xy in merged_xy:
            idx = len(new_frac)
            new_frac.append([float(xy[0] % 1.0), float(xy[1] % 1.0), float(z_center)])
            new_species.append(species0)
            layer_indices.append(idx)
        new_layers.append(layer_indices)

    metadata = {
        "enabled": True,
        "mode": "graphene_hexagon_centers",
        "reference_carbon_layer_index": reference_carbon_layer_index,
        "n_input_li_sites": int(len(li_frac)),
        "n_li_layers": int(len(li_layers)),
        "n_hollow_xy_before_dedupe": int(len(merged_xy_raw)),
        "n_hollow_xy_duplicates_removed": int(n_hollow_xy_duplicates_removed),
        "n_hollow_sites_per_li_layer": int(len(merged_xy)),
        "n_ga_li_candidate_sites": int(len(new_frac)),
        "cc_bond_cutoff_angstrom": float(cc_bond_cutoff),
        "hollow_site_dedupe_tol_angstrom": float(dedupe_tol_angstrom),
        "note": (
            "GA Li candidates include input Li sites plus graphene hexagon-center "
            "hollow sites; the requested SOC denominator remains the number of "
            "Li sites in the input structure."
        ),
    }
    return np.array(new_frac, dtype=float), new_species, new_layers, metadata


def build_same_layer_compatibility_matrix(
    lattice,
    li_frac: np.ndarray,
    li_layers: List[List[int]],
    min_distance_angstrom: float,
) -> Tuple[np.ndarray, dict]:
    """Return a boolean matrix of allowed Li-site pairs for GA configurations.

    Vectorised implementation: for each Li layer, all pairwise Cartesian
    distances are computed in one numpy operation instead of a triple Python loop.
    """
    n = len(li_frac)
    compatible = np.ones((n, n), dtype=bool)
    lat_mat = lattice.matrix                # (3, 3) row-vectors
    min_observed = float("inf")
    violating_pairs = 0

    for members in li_layers:
        if len(members) < 2:
            continue
        m = np.array(members, dtype=int)
        coords = li_frac[m]                         # (k, 3) fractional
        # Pairwise minimum-image fractional differences
        diff = coords[:, None, :] - coords[None, :, :]  # (k, k, 3)
        diff = diff - np.round(diff)
        # Convert to Cartesian: cart[i,j] = diff[i,j] @ lat_mat
        cart = np.tensordot(diff, lat_mat, axes=([-1], [0]))  # (k, k, 3)
        dists = np.linalg.norm(cart, axis=-1)        # (k, k)
        np.fill_diagonal(dists, np.inf)

        layer_min = float(np.min(dists))
        if layer_min < min_observed:
            min_observed = layer_min

        too_close = dists < min_distance_angstrom   # (k, k) upper+lower triangular
        ii_arr, jj_arr = np.where(too_close)
        for ii, jj in zip(ii_arr, jj_arr):
            compatible[int(m[ii]), int(m[jj])] = False
        violating_pairs += int(too_close.sum()) // 2  # each pair counted twice

    if min_observed == float("inf"):
        min_observed = None
    metadata = {
        "min_li_li_same_layer_distance_angstrom": float(min_distance_angstrom),
        "minimum_candidate_pair_distance_same_layer_angstrom": min_observed,
        "n_forbidden_same_layer_pairs": int(violating_pairs),
    }
    return compatible, metadata

