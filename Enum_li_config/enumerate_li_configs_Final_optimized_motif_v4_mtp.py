def main() -> None:
    args = parse_args()

    # ── Build supercell ────────────────────────────────────────────────────
    parent, N_LI_TOTAL, N_C_TOTAL = load_and_build_supercell(
        args.structure, args.supercell
    )
    li_idx, c_idx, li_frac, c_frac, li_species, c_species = extract_sublattices(parent)
    layers = detect_li_layers(li_frac)
    carbon_layers = detect_layers_by_z(c_frac, tol=args.carbon_layer_tol)

    n_carbon_layers = len(carbon_layers)
    all_a_sequence = "A" * n_carbon_layers

    if args.stacking_sequences is not None:
        requested_sequences = [x.upper() for x in args.stacking_sequences]
        if len(requested_sequences) == 1 and requested_sequences[0] == "ALL":
            stacking_sequences = generate_all_stacking_sequences(n_carbon_layers)
        else:
            stacking_sequences = validate_stacking_sequences(
                requested_sequences, n_carbon_layers
            )
    elif args.stacking_modes is not None:
        requested_sequences = []
        for mode in args.stacking_modes:
            mode_u = mode.upper()
            if mode_u == "AA":
                requested_sequences.append(all_a_sequence)
            elif mode_u == "AB":
                requested_sequences.append(alternating_ab_sequence(n_carbon_layers))
            else:
                raise ValueError(f"Unsupported stacking mode shortcut: {mode}")
        stacking_sequences = list(dict.fromkeys(requested_sequences))
    else:
        stacking_sequences = [all_a_sequence]

    if not stacking_sequences:
        raise ValueError("No stacking sequences were selected.")

    aa_only_soc_threshold = get_aa_only_soc_threshold(
        args.soc_stacking_policy, args.aa_only_soc_threshold
    )
    requested_stacking_sequences = list(stacking_sequences)
    # SOC-dependent high-SOC pruning may need the all-A sequence even if the
    # user requested only AB-like sequences. Add it to the internal sequence
    # pool so c_frac / Ewald / symmetry data are available when required.
    if aa_only_soc_threshold is not None and all_a_sequence not in stacking_sequences:
        stacking_sequences = [all_a_sequence] + stacking_sequences
        stacking_sequences = list(dict.fromkeys(stacking_sequences))
        logger.info(
            f"Added all-A stacking sequence {all_a_sequence} to support "
            f"--soc-stacking-policy {args.soc_stacking_policy}."
        )

    if args.ab_shift is None:
        b_shift, stacking_definition = auto_detect_b_layer_shift(
            parent.lattice, li_frac, c_frac, carbon_layers
        )
    else:
        b_shift = (args.ab_shift[0], args.ab_shift[1])
        stacking_definition = {
            "mode": "manual",
            "definition": {
                "A": "hexagon centers overlap between carbon layers",
                "B": "carbon layers shifted by the user-specified --ab-shift vector",
            },
            "b_shift_supercell_frac": [b_shift[0], b_shift[1]],
        }

    c_frac_by_stacking = {
        mode: make_carbon_stacking_fracs(c_frac, carbon_layers, mode, b_shift)
        for mode in stacking_sequences
    }

    layers_msg = f"Detected {len(layers)} Li layers along c (sizes: {[len(l) for l in layers]})"
    logger.info(layers_msg)
    logger.info(
        f"Detected {len(carbon_layers)} carbon layers along c "
        f"(sizes: {[len(l) for l in carbon_layers]})"
    )
    seq_preview = stacking_sequences[:8]
    more_seq = " ..." if len(stacking_sequences) > len(seq_preview) else ""
    logger.info(
        f"Stacking sequences ({len(stacking_sequences)}): {seq_preview}{more_seq}  |  "
        f"B-layer shift = ({b_shift[0]:.6f}, {b_shift[1]:.6f}) "
        f"in generated-supercell fractional coordinates  |  "
        f"definition={stacking_definition['mode']}"
    )
    if aa_only_soc_threshold is None:
        logger.info("SOC-dependent stacking policy: none; all requested stacking sequences are used at every SOC.")
    else:
        logger.info(
            f"SOC-dependent stacking policy: {args.soc_stacking_policy}; "
            f"SOC >= {aa_only_soc_threshold:.3f} uses only all-A stacking sequence {all_a_sequence}."
        )

    # ── GA Li candidate sites ─────────────────────────────────────────────
    # SOC levels and SOC values remain referenced to the Li sites present in
    # the input structure. In GA mode, however, Li can additionally occupy
    # empty graphene hollow sites in the same Li layers.
    ga_li_frac = li_frac
    ga_li_species = li_species
    ga_layers = layers
    ga_li_site_metadata = {
        "enabled": False,
        "n_input_li_sites": int(N_LI_TOTAL),
        "n_ga_li_candidate_sites": int(N_LI_TOTAL),
    }
    if args.ga and args.ga_expand_li_sites:
        ga_li_frac, ga_li_species, ga_layers, ga_li_site_metadata = expand_li_sites_with_hollow_centers(
            parent.lattice,
            li_frac,
            li_species,
            c_frac,
            carbon_layers,
            layers,
            cc_bond_cutoff=args.cc_bond_cutoff,
            dedupe_tol_angstrom=args.hollow_site_tol,
        )

    ga_compat_matrix, ga_distance_metadata = build_same_layer_compatibility_matrix(
        parent.lattice,
        ga_li_frac,
        ga_layers,
        args.ga_min_li_li_same_layer,
    )
    N_GA_LI_SITES = len(ga_li_frac)
    if args.ga:
        logger.info(
            f"GA Li candidate sites: {N_GA_LI_SITES} total "
            f"({ga_li_site_metadata.get('n_hollow_sites_per_li_layer', 'input-only')} per Li layer; "
            f"input Li sites={N_LI_TOTAL})"
        )
        logger.info(
            f"GA same-layer Li-Li constraint: d >= {args.ga_min_li_li_same_layer:.3f} Å  |  "
            f"forbidden candidate pairs={ga_distance_metadata['n_forbidden_same_layer_pairs']}"
        )

    # ── Estimated total structures & GA decision ─────────────────────────
    li_site_count_for_search = N_GA_LI_SITES if args.ga else N_LI_TOTAL
    half_n = li_site_count_for_search // 2
    max_combo = comb(li_site_count_for_search, half_n)
    soc_levels_preview = (
        args.soc_levels if args.soc_levels is not None
        else list(range(N_LI_TOTAL + 1))
    )
    total_li_est = sum(comb(li_site_count_for_search, n) for n in soc_levels_preview)
    total_candidate_est = 0
    for n_preview in soc_levels_preview:
        soc_preview = n_preview / N_LI_TOTAL
        active_preview, _ = active_stacking_sequences_for_soc(
            soc_preview, stacking_sequences, all_a_sequence,
            args.soc_stacking_policy, aa_only_soc_threshold,
        )
        total_candidate_est += comb(li_site_count_for_search, n_preview) * len(active_preview)
    logger.info(
        f"Estimated total ~{total_li_est:,} Li configurations per stacking sequence "
        f"using {li_site_count_for_search} Li candidate sites."
    )
    logger.info(
        f"SOC-policy-adjusted stacking-aware candidate space: "
        f"~{total_candidate_est:,} candidates across requested SOC levels."
    )

    use_ga_globally = args.ga
    logger.info(
        f"C({li_site_count_for_search},{half_n}) = {max_combo:,}  |  "
        f"GA {'ENABLED' if use_ga_globally else 'disabled'} "
        f"({'--ga set' if args.ga else '--ga not set'})"
    )

    # ── Symmetry for full-enumeration mode ────────────────────────────────
    mode_label = "no-symmetry" if args.no_symmetry else "symmetry-aware"
    perms: Optional[List[Tuple[int, ...]]] = None
    perms_by_stacking: Dict[str, Optional[List[Tuple[int, ...]]]] = {}
    if (not args.no_symmetry) and (not use_ga_globally):
        logger.info(
            "Preparing stacking-specific Li-site symmetry permutations for "
            "full enumeration."
        )
        for seq in stacking_sequences:
            logger.info(f"Symmetry analysis for stacking sequence {seq}")
            sym_struct = build_structure(
                parent.lattice, tuple(range(N_LI_TOTAL)),
                li_frac, li_species,
                c_frac_by_stacking[seq], c_species,
            )
            perms_by_stacking[seq] = get_li_site_permutations(
                sym_struct, li_frac, symprec=args.symprec, match_tol=args.match_tol
            )
        # Backward-compatible summary field: AA/all-A sequence if present,
        # otherwise the first requested sequence.
        perms = perms_by_stacking.get(all_a_sequence) or perms_by_stacking[stacking_sequences[0]]
    else:
        for seq in stacking_sequences:
            perms_by_stacking[seq] = None

    # ── Output directory ───────────────────────────────────────────────────
    out_dir = Path(args.output)
    out_dir.mkdir(parents=True, exist_ok=True)

    use_mtp_backend = args.energy_backend == "mtp-relax"
    mtp_evaluator: Optional[MTPRelaxationEvaluator] = None
    if use_mtp_backend:
        if not Path(args.mlip_ini).exists():
            logger.warning(
                f"Requested MTP backend but mlip.ini file was not found at {args.mlip_ini}. "
                "The script was generated successfully, but runtime MTP evaluation will fail until this file is present."
            )
        mtp_evaluator = MTPRelaxationEvaluator(
            mlp_bin=args.mlp_bin,
            mlip_ini=args.mlip_ini,
            output_root=out_dir,
            failed_energy=args.mtp_failed_energy,
            force_tolerance=args.mtp_force_tolerance,
            workers=args.mtp_workers,
            batch_size=args.mtp_batch_size,
            keep_workdirs=args.mtp_keep_workdirs,
            extra_args=args.mtp_extra_args,
        )
        logger.info(
            f"Energy backend: MTP relaxation via '{args.mlp_bin} relax {args.mlip_ini}' "
            f"(workers={args.mtp_workers}, batch_size={args.mtp_batch_size or 'all'})."
        )
    else:
        logger.info("Energy backend: Ewald / EwaldPrecomputed.")

    # ── Precompute Ewald matrices only when the Ewald backend is requested ──
    ewald_precomputed_by_stacking: Dict[str, EwaldPrecomputed] = {}
    if not use_mtp_backend:
        if use_ga_globally:
            logger.info(
                f"Precomputing GA Ewald matrices for {len(stacking_sequences)} "
                f"stacking sequence(s) with {len(ga_li_frac)} Li sites + "
                f"{N_C_TOTAL} C sites …"
            )
            for seq in stacking_sequences:
                ewald_precomputed_by_stacking[seq] = EwaldPrecomputed(
                    parent.lattice, ga_li_frac, ga_li_species,
                    c_frac_by_stacking[seq], c_species,
                )
            logger.info("GA Ewald precomputation complete.")
        else:
            logger.info(
                f"Precomputing Ewald matrices for {len(stacking_sequences)} "
                f"stacking sequence(s) with {N_LI_TOTAL} Li sites + "
                f"{N_C_TOTAL} C sites …"
            )
            for seq in stacking_sequences:
                ewald_precomputed_by_stacking[seq] = EwaldPrecomputed(
                    parent.lattice, li_frac, li_species,
                    c_frac_by_stacking[seq], c_species,
                )
            logger.info("Ewald precomputation complete.")

    # ── SOC level list ─────────────────────────────────────────────────────
    soc_levels = (
        args.soc_levels if args.soc_levels is not None
        else list(range(N_LI_TOTAL + 1))
    )
    invalid = [n for n in soc_levels if not (0 <= n <= N_LI_TOTAL)]
    if invalid:
        raise ValueError(f"Invalid n_Li values: {invalid}. Must be 0–{N_LI_TOTAL}.")

    # ── Summary skeleton ───────────────────────────────────────────────────
    sga = SpacegroupAnalyzer(parent, symprec=args.symprec)
    summary: dict = {
        "structure_file": str(Path(args.structure).resolve()),
        "supercell": args.supercell,
        "space_group": sga.get_space_group_symbol(),
        "space_group_number": sga.get_space_group_number(),
        "enumeration_mode": mode_label,
        "ga_enabled": use_ga_globally,
        "energy_backend": args.energy_backend,
        "mlip_ini": str(Path(args.mlip_ini).resolve()) if args.energy_backend == "mtp-relax" else None,
        "mlp_bin": args.mlp_bin if args.energy_backend == "mtp-relax" else None,
        "mtp_force_tolerance": args.mtp_force_tolerance if args.energy_backend == "mtp-relax" else None,
        "mtp_workers": args.mtp_workers if args.energy_backend == "mtp-relax" else None,
        "mtp_batch_size": args.mtp_batch_size if args.energy_backend == "mtp-relax" else None,
        "n_carbon_layers": n_carbon_layers,
        "requested_stacking_sequences": requested_stacking_sequences,
        "stacking_sequences": stacking_sequences,
        "soc_stacking_policy": args.soc_stacking_policy,
        "aa_only_soc_threshold": aa_only_soc_threshold,
        "ga_motif_seeding": args.ga_motif_seeding,
        "motif_seed_soc_threshold": args.motif_seed_soc_threshold,
        "motif_seed_fractions": dict(
            zip(
                ("rh_like_alternating", "facing_enhanced", "random"),
                normalize_fraction_triplet(
                    args.motif_rh_fraction, args.motif_facing_fraction, args.motif_random_fraction
                ),
            )
        ),
        "facing_mutation_prob": args.facing_mutation_prob,
        "ga_uniform_vacancy_mode": args.ga_uniform_vacancy_mode,
        "uniform_vacancy_tolerance": args.uniform_vacancy_tolerance,
        "aa_only_stacking_sequence": all_a_sequence,
        "stacking_policy_note": (
            "SOC-dependent high-SOC pruning follows the manuscript-inspired AA-dominant regime: "
            "only the all-A sequence is used at or above the configured SOC threshold."
            if aa_only_soc_threshold is not None else
            "No SOC-dependent stacking pruning was applied."
        ),
        "stacking_definition": stacking_definition,
        "b_layer_shift_supercell_frac": [b_shift[0], b_shift[1]],
        "stable_by_soc": [],
        "max_cif_per_soc": MAX_CIF_PER_SOC,
        "duplicate_structure_filter": args.dedup_mode,
        "duplicate_structure_filter_details": (
            "final top-20 selection: exact coordinate hash first, then symmetry-equivalence via pymatgen StructureMatcher until top 20 are found"
            if args.dedup_mode == "symmetry"
            else "final top-20 selection: exact fractional-coordinate set comparison"
            if args.dedup_mode == "exact"
            else "disabled"
        ),
        "dedup_ltol": args.dedup_ltol,
        "dedup_stol": args.dedup_stol,
        "dedup_angle_tol": args.dedup_angle_tol,
        "n_Li_total": N_LI_TOTAL,
        "n_ga_li_candidate_sites": int(N_GA_LI_SITES),
        "ga_li_candidate_site_generation": ga_li_site_metadata,
        "ga_li_li_distance_constraint": ga_distance_metadata,
        "n_C_total": N_C_TOTAL,
        "n_permutations": len(perms) if perms is not None else None,
        "soc_levels": [],
    }

    # ── Table header ───────────────────────────────────────────────────────
    header = (
        f"\n{'n_Li':>6}  {'SOC':>8}  {'C(N,k)':>12}  {'seq':>5}  "
        f"{'candidates':>13}  {'method':>12}  {'best E (eV)':>14}"
    )
    logger.info(header)
    logger.info("─" * 72)

    total_evaluated = 0

    for soc_idx, n_li in enumerate(soc_levels, start=1):
        li_site_count_this_method = N_GA_LI_SITES if use_ga_globally else N_LI_TOTAL
        n_raw = comb(li_site_count_this_method, n_li)
        soc = n_li / N_LI_TOTAL
        active_stacking_sequences, stacking_policy_info = active_stacking_sequences_for_soc(
            soc, stacking_sequences, all_a_sequence,
            args.soc_stacking_policy, aa_only_soc_threshold,
        )

        logger.info("")
        logger.info("=" * 72)
        logger.info(
            f"SOC {soc_idx}/{len(soc_levels)} START | "
            f"n_Li={n_li}/{N_LI_TOTAL}, SOC={soc:.4f}, "
            f"C({li_site_count_this_method},k)={n_raw:,}, "
            f"stacking_sequences={len(active_stacking_sequences)}, raw_candidates={n_raw * len(active_stacking_sequences):,}"
        )
        logger.info(
            f"SOC={soc:.4f} stacking policy: {stacking_policy_info['reason']}  |  "
            f"active_sequences={active_stacking_sequences}"
        )
        if use_ga_globally:
            approx_eval = args.ga_pop * args.ga_gens
            logger.info(
                f"GA mode: raw_candidates is the search-space size only; "
                f"full enumeration is not performed. Nominal population evaluations <= "
                f"~{approx_eval:,} before duplicate screening/cache effects."
            )
        logger.info("=" * 72)

        # Decide per-level method. GA is controlled only by explicit --ga.
        use_ga_here = use_ga_globally
        optimizer: Optional[GAOptimizer] = None   # defined here so summary block can safely check
        n_duplicates_removed = 0
        n_evaluated_raw = 0
        n_generated_raw = 0
        dedup_stats = {
            "n_candidates_input": 0,
            "n_exact_duplicates_removed": 0,
            "n_symmetry_duplicates_skipped_for_top20": 0,
            "n_symmetry_unique_cifs_written": 0,
            "dedup_mode": args.dedup_mode,
        }

        if use_ga_here:
            # ── GA path ──────────────────────────────────────────────────
            method = "GA"
            motif_active_here = motif_seeding_active_for_soc(
                args.ga_motif_seeding, soc, args.motif_seed_soc_threshold
            )
            motif_fracs = normalize_fraction_triplet(
                args.motif_rh_fraction, args.motif_facing_fraction, args.motif_random_fraction
            )
            if motif_active_here:
                logger.info(
                    f"SOC={soc:.4f} GA motif seeding active: "
                    f"RH-like/alternating={motif_fracs[0]:.2f}, "
                    f"facing-enhanced={motif_fracs[1]:.2f}, random={motif_fracs[2]:.2f}; "
                    f"facing_mutation_prob={args.facing_mutation_prob:.2f}, "
                    f"uniform_vacancy_mode={args.ga_uniform_vacancy_mode}"
                )
            optimizer = GAOptimizer(
                n_li_keep=n_li,
                n_li_total=N_GA_LI_SITES,
                li_frac=ga_li_frac,
                li_species=ga_li_species,
                c_frac_by_stacking=c_frac_by_stacking,
                c_species=c_species,
                lattice=parent.lattice,
                n_c=N_C_TOTAL,
                layers=ga_layers,
                compatible_pair_matrix=ga_compat_matrix,
                stacking_sequences=active_stacking_sequences,
                pop_size=args.ga_pop,
                n_generations=args.ga_gens,
                threshold_eV=args.ga_threshold_ev,
                frac_stacking_mutation=args.stacking_mutation_prob,
                motif_seeding_active=motif_active_here,
                motif_fractions=motif_fracs,
                facing_mutation_prob=args.facing_mutation_prob,
                uniform_vacancy_mode=args.ga_uniform_vacancy_mode,
                uniform_vacancy_tolerance=args.uniform_vacancy_tolerance,
                seed=args.ga_seed,
                ewald_precomputed=None if use_mtp_backend else ewald_precomputed_by_stacking,
                energy_evaluator=mtp_evaluator if use_mtp_backend else None,
                soc_dir=out_dir / f"SOC_{soc:.4f}_nLi{n_li:02d}",
            )
            try:
                reps, energies = optimizer.run()
            except NoValidGACandidateError as exc:
                logger.warning(
                    f"SOC={soc:.4f}: skipped GA search because no valid Li configuration "
                    f"satisfies d_Li-Li(same layer) >= {args.ga_min_li_li_same_layer:.3f} Å. "
                    f"Details: {exc}"
                )
                reps = []
                energies = []
                n_found = 0
                n_survivors = 0
                n_duplicates_removed = 0
                n_evaluated_raw = 0
                cif_records = []
            else:
                n_evaluated_raw = len(reps)
                reps, energies, dedup_stats = select_top_symmetry_unique_candidates(
                    reps, energies, parent.lattice,
                    ga_li_frac, ga_li_species, c_frac_by_stacking, c_species,
                    top_n=MAX_CIF_PER_SOC,
                    mode=args.dedup_mode,
                    matcher=make_structure_matcher(
                        args.dedup_ltol, args.dedup_stol, args.dedup_angle_tol
                    ) if args.dedup_mode == "symmetry" else None,
                )
                n_found = len(reps)
                n_survivors = optimizer.n_survivors_discovered
                n_duplicates_removed = (
                    dedup_stats.get("n_exact_duplicates_removed", 0)
                    + dedup_stats.get("n_symmetry_duplicates_skipped_for_top20", 0)
                )
                total_evaluated += len(optimizer.energy_cache)
                logger.info(
                    f"SOC={soc:.4f}: final selection from {n_evaluated_raw} GA-evaluated candidates "
                    f"(mode={args.dedup_mode}): exact duplicates removed="
                    f"{dedup_stats.get('n_exact_duplicates_removed', 0)}, "
                    f"symmetry duplicates skipped while selecting top {MAX_CIF_PER_SOC}="
                    f"{dedup_stats.get('n_symmetry_duplicates_skipped_for_top20', 0)}, "
                    f"symmetry-unique CIF candidates={n_found}."
                )

                # Convergence plot
                plot_path = (
                    out_dir
                    / f"SOC_{soc:.4f}_nLi{n_li:02d}"
                    / f"ga_convergence_nLi{n_li:02d}.png"
                )
                plot_path.parent.mkdir(parents=True, exist_ok=True)
                plot_ga_convergence(
                    optimizer.plot_data, n_li, N_LI_TOTAL,
                    args.ga_threshold_ev, plot_path
                )

                if n_found < MAX_CIF_PER_SOC:
                    logger.info(
                        f"SOC={soc:.4f}: {n_found} symmetry-unique candidates "
                        f"from {n_evaluated_raw} GA-evaluated candidates "
                        f"({n_survivors} GA survivors) (< {MAX_CIF_PER_SOC}) → "
                        f"writing all {n_found} as CIF"
                    )
                else:
                    logger.info(
                        f"SOC={soc:.4f}: {n_found} symmetry-unique candidates "
                        f"from {n_evaluated_raw} GA-evaluated candidates "
                        f"({n_survivors} GA survivors) → "
                        f"writing top {MAX_CIF_PER_SOC} by energy as CIF"
                    )

                if use_mtp_backend:
                    assert mtp_evaluator is not None
                    cif_records = write_soc_top_cifs_mtp(
                        n_li, N_LI_TOTAL, reps, energies,
                        parent, ga_li_frac, ga_li_species, c_frac_by_stacking, c_species,
                        out_dir, MAX_CIF_PER_SOC, mtp_evaluator,
                    )
                else:
                    cif_records = write_soc_top_cifs_ga(
                        n_li, N_LI_TOTAL, reps, energies,
                        parent, ga_li_frac, ga_li_species, c_frac_by_stacking, c_species,
                        out_dir, MAX_CIF_PER_SOC,
                    )

        else:
            # ── Full enumeration path ─────────────────────────────────────
            # Stacking sequences are considered even without --ga. Each
            # candidate is (stacking_sequence, occupied_Li_sites). For
            # symmetry-aware enumeration, Li-site representatives are generated
            # separately for each stacking sequence because AB-type shifts can
            # lower or change the supercell symmetry.
            method = "sym" if not args.no_symmetry else "all"
            candidates: List[Tuple[str, Tuple[int, ...]]] = []
            energies = []
            per_stacking_counts: Dict[str, int] = {}

            for seq in active_stacking_sequences:
                if args.no_symmetry:
                    reps_seq = enumerate_all(n_li, N_LI_TOTAL)
                else:
                    perms_seq = perms_by_stacking[seq]
                    if perms_seq is None:
                        raise RuntimeError(f"Missing symmetry permutations for stacking sequence {seq}.")
                    reps_seq = enumerate_symmetric(n_li, N_LI_TOTAL, perms_seq)

                per_stacking_counts[seq] = len(reps_seq)
                iterator = reps_seq
                if TQDM_AVAILABLE and len(reps_seq) > 50:
                    iterator = tqdm(
                        reps_seq,
                        desc=f"  Ewald n_Li={n_li} stacking={seq}",
                        leave=False,
                        unit="str",
                    )
                ep = None if use_mtp_backend else ewald_precomputed_by_stacking.get(seq)
                for occupied in iterator:
                    candidates.append((seq, occupied))
                    if not use_mtp_backend:
                        if ep is not None:
                            # Fast O(n_li²) precomputed path.
                            energies.append(ep.energy(occupied, n_li))
                        else:
                            # Fallback: full Ewald sum (original behaviour).
                            struct = build_structure(
                                parent.lattice, occupied, li_frac, li_species,
                                c_frac_by_stacking[seq], c_species
                            )
                            energies.append(compute_ewald_energy(struct, n_li, N_C_TOTAL))

            n_generated_raw = len(candidates)
            if use_mtp_backend:
                assert mtp_evaluator is not None
                soc_dir = out_dir / f"SOC_{soc:.4f}_nLi{n_li:02d}"
                energies = mtp_evaluator.evaluate_candidates(
                    candidates, parent.lattice, li_frac, li_species,
                    c_frac_by_stacking, c_species, soc_dir,
                    label=f"SOC_{soc:.4f}_nLi{n_li:02d}", generation=None,
                    aggregate_input_name="IN_SOC.cfg",
                    to_relax_name="to-relax.cfg",
                )

            reps, energies, dedup_stats = select_top_symmetry_unique_candidates(
                candidates, energies, parent.lattice,
                li_frac, li_species, c_frac_by_stacking, c_species,
                top_n=MAX_CIF_PER_SOC,
                mode=args.dedup_mode,
                matcher=make_structure_matcher(
                    args.dedup_ltol, args.dedup_stol, args.dedup_angle_tol
                ) if args.dedup_mode == "symmetry" else None,
            )
            n_found = len(reps)
            n_duplicates_removed = (
                dedup_stats.get("n_exact_duplicates_removed", 0)
                + dedup_stats.get("n_symmetry_duplicates_skipped_for_top20", 0)
            )
            total_evaluated += n_generated_raw

            logger.info(
                f"SOC={soc:.4f}: stacking-aware full enumeration counts before duplicate filtering: "
                + ", ".join(f"{seq}={count}" for seq, count in per_stacking_counts.items())
            )
            logger.info(
                f"SOC={soc:.4f}: final selection from {n_generated_raw} generated candidates "
                f"(mode={args.dedup_mode}): exact duplicates removed="
                f"{dedup_stats.get('n_exact_duplicates_removed', 0)}, "
                f"symmetry duplicates skipped while selecting top {MAX_CIF_PER_SOC}="
                f"{dedup_stats.get('n_symmetry_duplicates_skipped_for_top20', 0)}, "
                f"symmetry-unique CIF candidates={n_found}."
            )

            # POSCAR output is intentionally skipped for stacking-aware full
            # enumeration to avoid generating a very large number of files.
            # CIF output below still writes the most stable 20 candidates.
            if not args.no_poscar and n_li > 0:
                logger.info(
                    "POSCAR output skipped for stacking-aware full enumeration; "
                    "top CIF output is still written. Use CIF records in summary JSON."
                )

            if n_found < MAX_CIF_PER_SOC:
                logger.info(
                    f"SOC={soc:.4f}: {n_found} symmetry-unique stacking-aware candidates "
                    f"(< {MAX_CIF_PER_SOC}) → writing all {n_found} as CIF"
                )
            else:
                logger.info(
                    f"SOC={soc:.4f}: {n_found} symmetry-unique stacking-aware candidates → "
                    f"writing top {MAX_CIF_PER_SOC} by energy as CIF"
                )
            if use_mtp_backend:
                assert mtp_evaluator is not None
                cif_records = write_soc_top_cifs_mtp(
                    n_li, N_LI_TOTAL, reps, energies,
                    parent, li_frac, li_species, c_frac_by_stacking, c_species,
                    out_dir, MAX_CIF_PER_SOC, mtp_evaluator,
                )
            else:
                cif_records = write_soc_top_cifs_ga(
                    n_li, N_LI_TOTAL, reps, energies,
                    parent, li_frac, li_species, c_frac_by_stacking, c_species,
                    out_dir, MAX_CIF_PER_SOC,
                )

        best_energy = min(energies) if energies else float("nan")
        logger.info(
            f"{n_li:>6}  {soc:>8.4f}  {n_raw:>12,}  "
            f"{len(active_stacking_sequences):>5}  {n_found:>13,}  {method:>12}  {best_energy:>14.4f}"
        )
        best_idx = int(np.argmin(energies)) if energies else 0
        if reps:
            best_stacking_sequence, best_occupied = reps[best_idx]
            best_extra = f", best_stacking_sequence={best_stacking_sequence}"
        else:
            best_stacking_sequence = None
            best_occupied = ()
            best_extra = ", no_valid_structure=True"

        logger.info(
            f"SOC {soc_idx}/{len(soc_levels)} DONE  | "
            f"n_Li={n_li}/{N_LI_TOTAL}, SOC={soc:.4f}, method={method}, "
            f"found={n_found:,}, best_E={best_energy:.4f} eV{best_extra}"
        )
        logger.info(
            f"SOC={soc:.4f} most stable: stacking_sequence={best_stacking_sequence}, "
            f"E={best_energy:.6f} eV, occupied_Li_sites={list(best_occupied)}"
        )
        logger.info("-" * 72)

        soc_summary = {
            "n_Li": n_li,
            "SOC": round(soc, 6),
            "n_raw_combinations_per_stacking": n_raw,
            "n_stacking_sequences": len(active_stacking_sequences),
            "active_stacking_sequences": active_stacking_sequences,
            "stacking_policy": stacking_policy_info,
            "n_raw_stacking_candidates": n_raw * len(active_stacking_sequences),
            "n_unique_or_found": n_found,
            "n_duplicate_structures_removed": n_duplicates_removed,
            "n_evaluated_candidates": n_evaluated_raw if method == "GA" else n_generated_raw,
            "n_exact_duplicates_removed": int(dedup_stats.get("n_exact_duplicates_removed", 0)),
            "n_symmetry_duplicates_skipped_for_top20": int(dedup_stats.get("n_symmetry_duplicates_skipped_for_top20", 0)),
            "n_symmetry_unique_cifs_written": int(dedup_stats.get("n_symmetry_unique_cifs_written", n_found)),
            "dedup_mode": dedup_stats.get("dedup_mode", args.dedup_mode),
            "method": method,
            "best_energy_eV": round(best_energy, 6),
            "best_energy_backend": args.energy_backend,
            "best_ewald_energy_eV": None if use_mtp_backend else round(best_energy, 6),
            "best_mtp_relaxed_energy_eV": round(best_energy, 6) if use_mtp_backend else None,
            "best_stacking_sequence": best_stacking_sequence,
            "best_stacking": best_stacking_sequence,
            "best_occupied_Li_sites": list(best_occupied),
            "top_stable": cif_records,
        }
        if method == "GA":
            soc_summary["n_ga_survivors"] = n_survivors
            soc_summary["n_evaluated_candidates_before_duplicate_filter"] = n_evaluated_raw
            soc_summary["n_evaluated_unique_structures"] = n_found
            if optimizer is not None:
                soc_summary["n_ga_candidate_key_duplicates_removed"] = int(optimizer.n_candidate_key_duplicates_removed)
                soc_summary["n_ga_candidate_coordinate_duplicates_removed"] = int(optimizer.n_candidate_coordinate_duplicates_removed)
                soc_summary["ga_motif_seeding_active"] = bool(optimizer.motif_seeding_active)
                soc_summary["n_ga_motif_rh_like_seeds"] = int(optimizer.n_motif_rh_seeded)
                soc_summary["n_ga_motif_facing_seeds"] = int(optimizer.n_motif_facing_seeded)
                soc_summary["n_ga_motif_random_seeds"] = int(optimizer.n_motif_random_seeded)
                soc_summary["n_ga_facing_mutations_accepted"] = int(optimizer.n_facing_mutations_generated)
                soc_summary["n_ga_uniform_vacancy_rejections"] = int(optimizer.n_uniform_vacancy_rejections)
        else:
            soc_summary["n_generated_candidates_before_duplicate_filter"] = n_generated_raw
            soc_summary["n_per_stacking_unique_or_found_before_duplicate_filter"] = per_stacking_counts

        stable_record = {
            "n_Li": n_li,
            "SOC": round(soc, 6),
            "method": method,
            "best_stacking_sequence": best_stacking_sequence,
            "best_energy_eV": round(best_energy, 6),
            "best_energy_backend": args.energy_backend,
            "best_ewald_energy_eV": None if use_mtp_backend else round(best_energy, 6),
            "best_mtp_relaxed_energy_eV": round(best_energy, 6) if use_mtp_backend else None,
            "best_occupied_Li_sites": list(best_occupied),
        }
        summary["stable_by_soc"].append(stable_record)
        summary["soc_levels"].append(soc_summary)

    logger.info(f"\nTotal configurations evaluated: {total_evaluated:,}")

    logger.info("\nMost stable structure by SOC:")
    logger.info(
        f"{'n_Li':>6}  {'SOC':>8}  {'method':>8}  "
        f"{'stacking':>14}  {'best E (eV)':>14}"
    )
    logger.info("-" * 60)
    for row in summary["stable_by_soc"]:
        logger.info(
            f"{row['n_Li']:>6}  {row['SOC']:>8.4f}  {row['method']:>8}  "
            f"{str(row['best_stacking_sequence']):>14}  "
            f"{row['best_energy_eV']:>14.6f}"
        )

    # ── Write summary JSON ─────────────────────────────────────────────────
