def canonical_form(
    combo: Tuple[int, ...],
    perms: List[Tuple[int, ...]],
) -> Tuple[int, ...]:
    best = combo
    for perm in perms:
        image = tuple(sorted(perm[i] for i in combo))
        if image < best:
            best = image
    return best


def enumerate_symmetric(
    n_li_keep: int,
    n_li_total: int,
    perms: List[Tuple[int, ...]],
) -> List[Tuple[int, ...]]:
    total = comb(n_li_total, n_li_keep)
    seen: set = set()
    reps: List[Tuple[int, ...]] = []
    iterator = combinations(range(n_li_total), n_li_keep)
    if TQDM_AVAILABLE and total > 500:
        iterator = tqdm(iterator, total=total,
                        desc=f"  n_Li={n_li_keep:2d} (sym)", leave=False, unit="combo")
    elif total > 500:
        logger.info(f"  Scanning {total:,} combinations (symmetry-aware)...")
    for combo in iterator:
        canon = canonical_form(combo, perms)
        if canon not in seen:
            seen.add(canon)
            reps.append(canon)
    return reps


def enumerate_all(n_li_keep: int, n_li_total: int) -> List[Tuple[int, ...]]:
    total = comb(n_li_total, n_li_keep)
    if total > 500:
        logger.info(f"  Generating all {total:,} combinations (no symmetry)...")
    return list(combinations(range(n_li_total), n_li_keep))


