def write_soc_top_cifs_mtp(
    n_li_keep: int,
    n_li_total: int,
    candidates: List[Tuple[str, Tuple[int, ...]]],
    energies: List[float],
    parent: Structure,
    li_frac: np.ndarray,
    li_species: list,
    c_frac_by_stacking: Dict[str, np.ndarray],
    c_species: list,
    out_dir: Path,
    top_n: int,
    mtp_evaluator: MTPRelaxationEvaluator,
) -> List[dict]:
    """Write top structures as CIF, preferring relaxed MTP geometries."""
    soc = n_li_keep / n_li_total
    cif_dir = out_dir / f"SOC_{soc:.4f}_nLi{n_li_keep:02d}" / "top_stable_cif"
    cif_dir.mkdir(parents=True, exist_ok=True)
    sorted_pairs = sorted(zip(energies, candidates), key=lambda x: x[0])
    records = []
    for rank, (energy, candidate) in enumerate(sorted_pairs[:top_n], start=1):
        stacking, occupied = candidate
        struct = mtp_evaluator.relaxed_structure_cache.get(candidate)
        used_relaxed = struct is not None
        if struct is None:
            struct = build_structure(
                parent.lattice, occupied,
                li_frac, li_species,
                c_frac_by_stacking[stacking], c_species,
            )
        n_atoms = len(struct)
        e_per_atom = energy / n_atoms if n_atoms else 0.0
        fname = (
            f"rank{rank:03d}_{stacking}_nLi{n_li_keep:02d}"
            f"_SOC{soc:.4f}_MTP_E{energy:.6f}eV.cif"
        )
        CifWriter(struct).write_file(str(cif_dir / fname))
        records.append({
            "rank": rank,
            "stacking_sequence": stacking,
            "stacking": stacking,
            "occupied_Li_sites": list(occupied),
            "mtp_relaxed_energy_eV": round(float(energy), 8),
            "mtp_relaxed_energy_per_atom_eV": round(float(e_per_atom), 10),
            "relaxed_geometry_used": bool(used_relaxed),
            "cif_file": str(cif_dir / fname),
        })
    return records


