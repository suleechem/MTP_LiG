def plot_ga_convergence(
    plot_data: List[List[Tuple[float, str]]],
    n_li: int,
    n_li_total: int,
    threshold_eV: float,
    out_path: Path,
) -> None:
    if not MATPLOTLIB_AVAILABLE:
        logger.warning("matplotlib not available – skipping GA plot.")
        return

    style = {
        "random":        ("blue",   "Random",                    20, 0.35),
        "vacancy_swap":  ("red",    "Mutation (Vacancy swap)",   20, 0.35),
        "layer_swap":    ("orange", "Mutation (Li layer swap)",  20, 0.35),
        "stacking_flip":    ("purple", "Mutation (Stacking flip)",  20, 0.35),
        "facing_mutation":  ("brown",  "Mutation (Facing pair)",    20, 0.35),
        "rh_seed":          ("cyan",   "Seed (RH-like)",            22, 0.45),
        "facing_seed":      ("magenta","Seed (Facing)",            22, 0.45),
        "survivor":         ("green",  "Survivor",                  18, 0.85),
    }

    xs: Dict[str, List[float]] = {k: [] for k in style}
    ys: Dict[str, List[float]] = {k: [] for k in style}

    for gen_idx, gen_data in enumerate(plot_data):
        for energy, ptype in gen_data:
            if ptype in xs:
                xs[ptype].append(gen_idx + 1)
                ys[ptype].append(energy)

    fig, ax = plt.subplots(figsize=(10, 6))

    # Plot in z-order: non-survivors first, survivors on top
    for ptype in ("random", "rh_seed", "facing_seed", "vacancy_swap", "layer_swap", "facing_mutation", "stacking_flip", "survivor"):
        if not xs[ptype]:
            continue
        color, label, size, alpha = style[ptype]
        ax.scatter(xs[ptype], ys[ptype],
                   c=color, label=label, s=size, alpha=alpha,
                   edgecolors="none", zorder=3 if ptype == "survivor" else 2)

    # Threshold annotation
    all_e = [e for gen in plot_data for e, _ in gen]
    global_best = min(all_e)
    thresh_line = global_best + threshold_eV

    ax.axhline(thresh_line, color="black", linestyle="--", linewidth=1.0,
               label=f"threshold : {threshold_eV} eV", zorder=4)

    ymin_data, ymax_data = min(all_e), max(all_e)
    span = ymax_data - ymin_data
    if span <= 1e-12:
        margin = max(abs(ymin_data) * 0.05, 0.1)
    else:
        margin = span * 0.08
    ax.set_ylim(ymin_data - margin, ymax_data + margin * 2)

    # Shade survivor region
    ybot = ax.get_ylim()[0]
    ax.axhspan(ybot, thresh_line, alpha=0.06, color="green", zorder=0)

    # Threshold label inside the box
    n_gens = len(plot_data)
    ax.text(
        n_gens * 0.72, (ybot + thresh_line) / 2,
        f"threshold : {threshold_eV} eV",
        fontsize=10, color="black",
        bbox=dict(boxstyle="round,pad=0.2", fc="white", ec="none", alpha=0.7),
    )

    ax.set_xlabel("Generation", fontsize=13)
    ax.set_ylabel("Energy (eV)", fontsize=13)
    soc = n_li / n_li_total
    ax.set_title(
        f"GA Convergence  —  nLi = {n_li}/{n_li_total}  (SOC = {soc:.4f})",
        fontsize=13,
    )
    ax.legend(loc="upper right", framealpha=0.9, fontsize=10)
    ax.set_xlim(0.5, n_gens + 0.5)
    plt.tight_layout()
    plt.savefig(str(out_path), dpi=150)
    plt.close()
    logger.info(f"  GA convergence plot → {out_path}")


