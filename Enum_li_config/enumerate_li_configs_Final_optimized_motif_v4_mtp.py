#!/usr/bin/env python3
"""
enumerate_li_configs_Final_optimized_motif_v2.py

Enumerate Li-vacancy configurations in a graphite intercalation compound.
Reads a unit-cell CIF (or POSCAR), builds an a×b×c supercell, then enumerates
Li arrangements at each SOC level (fraction of Li sites occupied).

Two enumeration modes:
  default        – symmetry-aware: one representative per inequivalent class
  --no-symmetry  – brute-force: all C(N,k) combinations (no deduplication)

Genetic algorithm (GA) search is applied only when --ga is
explicitly specified. When --ga is specified, GA is used for every requested
SOC level. A convergence plot is generated for each GA level.

The most stable 20 structures at most are written as CIF per SOC.

The search candidate can include both carbon-layer stacking sequence and Li
configuration, in both GA and full-enumeration modes. For a 4-carbon-layer
supercell, examples are AAAA, ABAA, AABA, and ABAB. Use --stacking-sequences
ALL to search all A/B stacking sequences with the first layer fixed to A. By
default, B layers are generated by an auto-detected in-plane shift that places
a carbon vertex above/below a graphene hexagon center, i.e. Bernal-like AB
registry.

Literature-based SOC-dependent stacking policy (--soc-stacking-policy):
  conservative  – AA-only stacking for SOC >= 0.50
  literature    – AA-only stacking for SOC >= 0.40 (Kim et al., ACS Nano)

Motif-biased GA seeding (--ga-motif-seeding):
  high-soc  – RH-like / facing-pair seeds active above --motif-seed-soc-threshold
  always    – motif seeding at all SOC levels
  none      – purely random initial populations

Performance notes (v2 optimisations):
  - EwaldPrecomputed: per-config energy O(n_li²) instead of O((n_li+n_c)²)
  - _can_add_site(): O(k) single-site compatibility check replaces O(k²) full
    matrix rebuild in all inner GA loops (_random_occupied, _random_balanced_occupied,
    _facing_enhanced_occupied, _layer_swap)
  - _vacancy_swap(): redundant _is_valid_occupied() removed after
    _compatible_add_sites() already guarantees validity
  - _sites_by_xy_column(): result cached after first call (li_frac is fixed)
  - _compatible_add_sites(): vacant_mask built with vectorised numpy indexing
  - _random_balanced_occupied(): O(N²) list-comprehension replaced by O(1)
    per-layer counter variable
  - JSON summary: normalize_fraction_triplet() called once, not three times

Usage:
    python enumerate_li_configs_Final_optimized_motif_v2.py --structure LiC6.cif
    python enumerate_li_configs_Final_optimized_motif_v2.py --structure LiC6.cif -a 2 -b 2 -c 3
    python enumerate_li_configs_Final_optimized_motif_v2.py --structure LiC6.cif --no-symmetry --soc-levels 4 6
    python enumerate_li_configs_Final_optimized_motif_v2.py --structure LiC6.cif --ga
    python enumerate_li_configs_Final_optimized_motif_v2.py --structure LiC6.cif --stacking-sequences ALL
    python enumerate_li_configs_Final_optimized_motif_v2.py --structure LiC6.cif --ga --stacking-sequences ALL
    python enumerate_li_configs_Final_optimized_motif_v2.py --structure LiC6.cif --ga --stacking-sequences ALL --soc-stacking-policy literature
    python enumerate_li_configs_Final_optimized_motif_v2.py --structure LiC6.cif --ga --ga-motif-seeding high-soc
    python enumerate_li_configs_Final_optimized_motif_v2.py --structure LiC6.cif --ga --ga-min-li-li-same-layer 4.35
"""

