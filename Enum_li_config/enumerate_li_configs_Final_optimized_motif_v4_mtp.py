def normalize_fraction_triplet(a: float, b: float, c: float) -> Tuple[float, float, float]:
    """Normalize three non-negative fractions; fall back to 0.7/0.2/0.1."""
    vals = [max(0.0, float(a)), max(0.0, float(b)), max(0.0, float(c))]
    total = sum(vals)
    if total <= 1e-12:
        return 0.70, 0.20, 0.10
    return vals[0] / total, vals[1] / total, vals[2] / total


def motif_seeding_active_for_soc(mode: str, soc: float, threshold: float) -> bool:
    """Return whether motif seeding should be active for this SOC."""
    if mode == "none":
        return False
    if mode == "always":
        return True
    if mode == "high-soc":
        return soc >= threshold
    raise ValueError(f"Unsupported --ga-motif-seeding mode: {mode}")

class NoValidGACandidateError(RuntimeError):
    """Raised when no Li configuration satisfies the GA hard constraints."""


class GAOptimizer:
    """
    Genetic-algorithm-style evolutionary search for stable Li configurations.

    In GA mode, a candidate is:
        (stacking_sequence, occupied_Li_sites)

    occupied_Li_sites are indices in the GA Li candidate-site pool. This pool
    may be larger than the Li sites explicitly present in the input structure,
    because empty graphene hexagon-center hollow sites can be added for GA.

    Example:
        ("AA", (0, 3, 7, 10))
        ("AB", (0, 3, 7, 10))

    Each generation:
      - Survivors   : candidates within threshold_eV of generation minimum
      - New configs : random | vacancy-swap | Li-layer-swap | stacking-flip

    This is a mutation-selection GA without crossover.
    """

    def __init__(
        self,
        n_li_keep: int,
        n_li_total: int,
        li_frac: np.ndarray,
        li_species: list,
        c_frac_by_stacking: Dict[str, np.ndarray],
        c_species: list,
        lattice,
        n_c: int,
        layers: List[List[int]],
        compatible_pair_matrix: Optional[np.ndarray],
        stacking_sequences: List[str],
        pop_size: int = 120,
        n_generations: int = 60,
        threshold_eV: float = 0.5,
        frac_random: float = 0.25,
        frac_stacking_mutation: float = 0.15,
        motif_seeding_active: bool = False,
        motif_fractions: Tuple[float, float, float] = (0.70, 0.20, 0.10),
        facing_mutation_prob: float = 0.20,
        uniform_vacancy_mode: str = "seed",
        uniform_vacancy_tolerance: int = 1,
        seed: int = 42,
        ewald_precomputed: Optional[Dict[str, "EwaldPrecomputed"]] = None,
        energy_evaluator: Optional[MTPRelaxationEvaluator] = None,
        soc_dir: Optional[Path] = None,
    ):
        self.n_li_keep = n_li_keep
        self.n_li_total = n_li_total
        self.li_frac = li_frac
        self.li_species = li_species
        self.c_frac_by_stacking = c_frac_by_stacking
        self.c_species = c_species
        self.lattice = lattice
        self.n_c = n_c
        self.layers = layers
        self.compatible_pair_matrix = compatible_pair_matrix
        self.stacking_sequences = [m.upper() for m in stacking_sequences]
        self.pop_size = pop_size
        self.n_generations = n_generations
        self.threshold_eV = threshold_eV
        self.frac_random = frac_random
        self.frac_stacking_mutation = frac_stacking_mutation
        self.motif_seeding_active = bool(motif_seeding_active)
        self.motif_rh_fraction, self.motif_facing_fraction, self.motif_random_fraction = motif_fractions
        self.facing_mutation_prob = max(0.0, float(facing_mutation_prob))
        self.uniform_vacancy_mode = uniform_vacancy_mode
        self.uniform_vacancy_tolerance = int(uniform_vacancy_tolerance)
        self.n_motif_rh_seeded = 0
        self.n_motif_facing_seeded = 0
        self.n_motif_random_seeded = 0
        self.n_facing_mutations_generated = 0
        self.n_uniform_vacancy_rejections = 0
        self.rng = random.Random(seed)
        # Lazy cache for _sites_by_xy_column (li_frac is fixed during GA)
        self._xy_columns_cache: Optional[Dict[Tuple[float, float], List[int]]] = None
        # Optional precomputed Ewald matrices keyed by stacking sequence.
        # When present, _energy() skips Structure building and full Ewald sums.
        self.ewald_precomputed: Optional[Dict[str, EwaldPrecomputed]] = ewald_precomputed
        self.energy_evaluator = energy_evaluator
        self.soc_dir = Path(soc_dir) if soc_dir is not None else None

        # Candidate key: (stacking_sequence, occupied_Li_sites)
        # If an external MTP evaluator is used, this dictionary is synchronized
        # with the evaluator cache so the rest of the GA bookkeeping remains unchanged.
        self.energy_cache: Dict[Tuple[str, Tuple[int, ...]], float] = {}
        self.n_survivors_discovered = 0
        self.valid_occupied_pool: Optional[List[Tuple[int, ...]]] = None
        self.valid_occupied_pool_limited = False
        self.n_candidate_key_duplicates_removed = 0
        self.n_candidate_coordinate_duplicates_removed = 0

        # For plotting: list of list of (energy, type_str)
        self.plot_data: List[List[Tuple[float, str]]] = []

    # ── Energy (cached) ────────────────────────────────────────────────────

    def _energy(self, candidate: Tuple[str, Tuple[int, ...]]) -> float:
        return self._energies_for_population([candidate], generation=None)[0]

    def _energies_for_population(
        self,
        population: List[Tuple[str, Tuple[int, ...]]],
        generation: Optional[int],
    ) -> List[float]:
        missing = [c for c in population if c not in self.energy_cache]
        if missing:
            if self.energy_evaluator is not None:
                if self.soc_dir is None:
                    raise RuntimeError("MTP energy evaluator requires soc_dir.")
                label = "ga_single" if generation is None else f"ga_gen{generation:03d}"
                self.energy_evaluator.evaluate_candidates(
                    missing, self.lattice, self.li_frac, self.li_species,
                    self.c_frac_by_stacking, self.c_species,
                    self.soc_dir, label=label, generation=generation,
                    aggregate_input_name=f"IN_{generation:03d}.cfg" if generation is not None else "IN_single.cfg",
                )
                for c in missing:
                    self.energy_cache[c] = self.energy_evaluator.energy_cache[c]
            else:
                for candidate in missing:
                    stacking, occupied = candidate
                    if (
                        self.ewald_precomputed is not None
                        and stacking in self.ewald_precomputed
                    ):
                        # Fast path: O(n_li_keep²) matrix slice — no Structure build needed.
                        e = self.ewald_precomputed[stacking].energy(occupied, self.n_li_keep)
                    else:
                        # Fallback: full Ewald sum via pymatgen (original behaviour).
                        struct = build_structure(
                            self.lattice, occupied,
                            self.li_frac, self.li_species,
                            self.c_frac_by_stacking[stacking], self.c_species,
                        )
                        e = compute_ewald_energy(struct, self.n_li_keep, self.n_c)
                    self.energy_cache[candidate] = e
        return [self.energy_cache[c] for c in population]

    # ── Config generators ──────────────────────────────────────────────────

    def _layer_counts(self, config: Tuple[int, ...]) -> List[int]:
        occ = set(config)
        return [sum(1 for site in layer if site in occ) for layer in self.layers]

    def _layer_vacancy_counts(self, config: Tuple[int, ...]) -> List[int]:
        occ = set(config)
        return [sum(1 for site in layer if site not in occ) for layer in self.layers]

    def _has_uniform_vacancies(self, config: Tuple[int, ...]) -> bool:
        if not self.layers:
            return True
        vac = self._layer_vacancy_counts(config)
        if not vac:
            return True
        return (max(vac) - min(vac)) <= self.uniform_vacancy_tolerance

    def _can_add_site(self, site: int, existing: List[int]) -> bool:
        """O(len(existing)) compatibility check for adding one site to a partial config.

        Cheaper than _is_valid_occupied() for incremental construction because it
        only tests the new site against each already-chosen site (not the full
        k×k submatrix).  Does NOT check the uniform-vacancy constraint.
        """
        if self.compatible_pair_matrix is None or not existing:
            return True
        occ = np.asarray(existing, dtype=int)
        return bool(np.all(self.compatible_pair_matrix[site, occ]))

    def _xy_column_key(self, site: int, decimals: int = 6) -> Tuple[float, float]:
        xy = np.mod(self.li_frac[site, :2], 1.0)
        xy = np.round(xy, decimals=decimals)
        xy[np.isclose(xy, 1.0, atol=10 ** (-decimals))] = 0.0
        return float(xy[0]), float(xy[1])

    def _sites_by_xy_column(self) -> Dict[Tuple[float, float], List[int]]:
        """Return {xy_key: [site_indices]} dict. Result is cached (li_frac is fixed during GA)."""
        if self._xy_columns_cache is not None:
            return self._xy_columns_cache
        columns: Dict[Tuple[float, float], List[int]] = {}
        for site in range(self.n_li_total):
            columns.setdefault(self._xy_column_key(site), []).append(site)
        self._xy_columns_cache = columns
        return columns

    def _target_layer_counts_balanced(self) -> List[int]:
        """Balanced Li counts per Li layer, equivalent to uniform vacancy distribution when layer sizes match."""
        n_layers = len(self.layers)
        if n_layers == 0:
            return []
        base = self.n_li_keep // n_layers
        rem = self.n_li_keep % n_layers
        counts = [base] * n_layers
        # Randomize which layers receive the +1 remainder so repeated seeds are diverse.
        order = list(range(n_layers))
        self.rng.shuffle(order)
        for idx in order[:rem]:
            counts[idx] += 1
        # Clip to layer sizes in case uneven candidate pools exist.
        counts = [min(c, len(self.layers[i])) for i, c in enumerate(counts)]
        # If clipping reduced the total, add remaining sites greedily where capacity exists.
        deficit = self.n_li_keep - sum(counts)
        while deficit > 0:
            changed = False
            for i in order:
                if counts[i] < len(self.layers[i]):
                    counts[i] += 1
                    deficit -= 1
                    changed = True
                    if deficit == 0:
                        break
            if not changed:
                break
        return counts

    def _random_balanced_occupied(self, max_attempts: int = 500) -> Tuple[int, ...]:
        """Generate a layer-balanced/RH-like candidate with approximately uniform vacancies.

        Optimised: uses a per-layer counter (O(1) check) instead of a list
        comprehension over all chosen sites (was O(N²)).  Single-site
        compatibility is checked with _can_add_site() — O(n_li_keep) — rather
        than rebuilding a sorted tuple and calling _is_valid_occupied() — O(n²).
        """
        if self.n_li_keep == 0:
            return ()
        for _ in range(max_attempts):
            target_counts = self._target_layer_counts_balanced()
            chosen: List[int] = []
            failed = False
            for layer_idx, layer_sites in enumerate(self.layers):
                need = target_counts[layer_idx] if layer_idx < len(target_counts) else 0
                candidates = list(layer_sites)
                self.rng.shuffle(candidates)
                layer_count = 0  # sites added from this layer in this attempt
                for site in candidates:
                    if layer_count >= need:
                        break
                    if self._can_add_site(site, chosen):
                        chosen.append(site)
                        layer_count += 1
                if layer_count < need:
                    failed = True
                    break
            if not failed and len(chosen) == self.n_li_keep:
                config = tuple(sorted(chosen))
                if self.uniform_vacancy_mode in ("seed", "hard") and not self._has_uniform_vacancies(config):
                    continue
                return config
        logger.debug(
            "  _random_balanced_occupied: fell back to _random_occupied "
            f"after {max_attempts} attempts"
        )
        return self._random_occupied()

    def _facing_enhanced_occupied(self, max_attempts: int = 500) -> Tuple[int, ...]:
        """Generate a candidate that preferentially fills the same in-plane columns across Li layers."""
        if self.n_li_keep == 0:
            return ()
        columns = list(self._sites_by_xy_column().values())
        # Prefer columns that span multiple Li layers; those create interlayer facing pairs.
        columns.sort(key=lambda col: len(col), reverse=True)
        for _ in range(max_attempts):
            chosen: List[int] = []
            cols = [list(col) for col in columns]
            self.rng.shuffle(cols)
            cols.sort(key=lambda col: len(col), reverse=True)
            for col in cols:
                self.rng.shuffle(col)
                for site in col:
                    if len(chosen) >= self.n_li_keep:
                        break
                    if self._can_add_site(site, chosen):
                        chosen.append(site)
                if len(chosen) >= self.n_li_keep:
                    break
            # Fill any remaining sites with valid random additions.
            attempts = 0
            while len(chosen) < self.n_li_keep and attempts < 200:
                attempts += 1
                remaining = set(chosen)
                valid_add = self._compatible_add_sites(remaining)
                if not valid_add:
                    break
                site = self.rng.choice(valid_add)
                if site not in remaining:
                    chosen.append(site)
            if len(chosen) == self.n_li_keep:
                config = tuple(sorted(chosen))
                if self.uniform_vacancy_mode in ("seed", "hard") and not self._has_uniform_vacancies(config):
                    # Facing seeds should still respect approximately uniform vacancy distribution.
                    continue
                return config
        return self._random_balanced_occupied()

    def _motif_candidate(self, motif_type: str) -> Tuple[str, Tuple[int, ...]]:
        stacking = self.rng.choice(self.stacking_sequences)
        if motif_type == "rh":
            return stacking, self._random_balanced_occupied()
        if motif_type == "facing":
            return stacking, self._facing_enhanced_occupied()
        return stacking, self._random_occupied()

    def _is_valid_occupied(self, config: Tuple[int, ...]) -> bool:
        """Check the same-layer Li-Li distance constraint for one configuration."""
        if self.uniform_vacancy_mode == "hard" and self.motif_seeding_active:
            if not self._has_uniform_vacancies(config):
                self.n_uniform_vacancy_rejections += 1
                return False
        if self.compatible_pair_matrix is None or len(config) <= 1:
            return True
        occ = np.asarray(config, dtype=int)
        # Extract the sub-matrix for occupied sites and verify all pairs allowed.
        return bool(np.all(self.compatible_pair_matrix[np.ix_(occ, occ)]))

    def _enumerate_valid_occupied_pool(self, limit: int = 50000) -> List[Tuple[int, ...]]:
        """Enumerate valid Li occupations up to *limit* under the hard-core constraint.

        This is used as a fallback for high-SOC levels where naive random or
        greedy generation can fail, and also to detect infeasible SOC levels
        without crashing the entire run.
        """
        if self.valid_occupied_pool is not None:
            return self.valid_occupied_pool

        pool: List[Tuple[int, ...]] = []
        chosen: List[int] = []
        self.valid_occupied_pool_limited = False

        if self.n_li_keep == 0:
            self.valid_occupied_pool = [()]
            return self.valid_occupied_pool
        if self.n_li_keep > self.n_li_total:
            self.valid_occupied_pool = []
            return self.valid_occupied_pool

        def can_add(site: int) -> bool:
            if self.compatible_pair_matrix is None:
                return True
            return all(self.compatible_pair_matrix[site, other] for other in chosen)

        def backtrack(start: int) -> None:
            if len(pool) >= limit:
                self.valid_occupied_pool_limited = True
                return
            if len(chosen) == self.n_li_keep:
                pool.append(tuple(chosen))
                return
            # Not enough remaining sites to complete the configuration.
            remaining_needed = self.n_li_keep - len(chosen)
            if self.n_li_total - start < remaining_needed:
                return

            for site in range(start, self.n_li_total):
                if self.n_li_total - site < remaining_needed:
                    break
                if can_add(site):
                    chosen.append(site)
                    backtrack(site + 1)
                    chosen.pop()
                    if len(pool) >= limit:
                        return

        backtrack(0)
        self.valid_occupied_pool = pool
        return self.valid_occupied_pool

    def _random_occupied(self) -> Tuple[int, ...]:
        """Generate a random Li occupation satisfying the same-layer distance constraint."""
        if self.n_li_keep == 0:
            return ()
        if self.n_li_keep > self.n_li_total:
            raise NoValidGACandidateError(
                f"Cannot place {self.n_li_keep} Li atoms on only {self.n_li_total} candidate sites."
            )

        # First try greedy randomized construction; this is fast when valid
        # configurations are not too rare.
        # _can_add_site() is O(n_li_keep) per site instead of the O(n_li_keep²)
        # full-matrix check previously done via _is_valid_occupied().
        for _ in range(3000):
            order = list(range(self.n_li_total))
            self.rng.shuffle(order)
            chosen: List[int] = []
            for site in order:
                if self._can_add_site(site, chosen):
                    chosen.append(site)
                    if len(chosen) == self.n_li_keep:
                        return tuple(sorted(chosen))

        # Fallback: construct a valid pool by backtracking. This prevents a
        # high-SOC run from failing simply because random generation is
        # inefficient, and it also proves infeasibility when the pool is empty.
        pool = self._enumerate_valid_occupied_pool(limit=max(50000, self.pop_size * 500))
        if not pool:
            raise NoValidGACandidateError(
                f"No valid GA Li configuration exists with n_Li={self.n_li_keep} under the current "
                f"same-layer Li-Li distance constraint. Try lowering --soc-levels, lowering "
                f"--ga-min-li-li-same-layer, or disabling expanded hollow sites with "
                f"--no-ga-expand-li-sites."
            )
        return self.rng.choice(pool)

    def _random_candidate(self) -> Tuple[str, Tuple[int, ...]]:
        stacking = self.rng.choice(self.stacking_sequences)
        return stacking, self._random_occupied()

    def _candidate_exact_key(self, candidate: Tuple[str, Tuple[int, ...]]):
        stacking, occupied = candidate
        return structure_duplicate_key(
            occupied,
            self.li_frac,
            self.c_frac_by_stacking[stacking],
        )

    def _append_unique_candidate(
        self,
        population: List[Tuple[str, Tuple[int, ...]]],
        pop_types: List[str],
        candidate: Tuple[str, Tuple[int, ...]],
        candidate_type: str,
        seen_candidate_keys: set,
        seen_exact_keys: set,
    ) -> bool:
        """Append a GA individual only if its key and exact coordinate hash are new in this generation."""
        if candidate in seen_candidate_keys:
            self.n_candidate_key_duplicates_removed += 1
            return False
        exact_key = self._candidate_exact_key(candidate)
        if exact_key in seen_exact_keys:
            self.n_candidate_coordinate_duplicates_removed += 1
            return False
        seen_candidate_keys.add(candidate)
        seen_exact_keys.add(exact_key)
        population.append(candidate)
        pop_types.append(candidate_type)
        return True

    def _compatible_add_sites(self, remaining: set) -> List[int]:
        """Vacant sites that can be added to a partially occupied set.

        Vectorised: builds a boolean mask over all vacant sites in one numpy
        operation rather than a Python loop with _is_valid_occupied per site.
        """
        if self.compatible_pair_matrix is None:
            return [s for s in range(self.n_li_total) if s not in remaining]

        all_sites = np.arange(self.n_li_total, dtype=int)
        # Vectorised: set occupied positions False in one numpy call.
        vacant_mask = np.ones(self.n_li_total, dtype=bool)
        if remaining:
            vacant_mask[np.fromiter(remaining, dtype=int)] = False
        vacant = all_sites[vacant_mask]               # indices of vacant sites

        if len(remaining) == 0:
            return [int(x) for x in vacant]

        occ = np.fromiter(remaining, dtype=int)
        # compat_sub[v, :] = compatible_pair_matrix[vacant[v], occ] for each v
        compat_sub = self.compatible_pair_matrix[np.ix_(vacant, occ)]  # (|vacant|, |occ|)
        valid_mask = np.all(compat_sub, axis=1)
        return [int(x) for x in vacant[valid_mask]]

    def _vacancy_swap(
        self,
        candidate: Tuple[str, Tuple[int, ...]],
    ) -> Tuple[str, Tuple[int, ...]]:
        stacking, config = candidate
        occupied = set(config)
        if not occupied:
            return candidate
        # _compatible_add_sites() already guarantees pair-constraint validity,
        # so a second _is_valid_occupied() call is redundant.
        for _ in range(100):
            remove = self.rng.choice(list(occupied))
            remaining = occupied - {remove}
            valid_add = self._compatible_add_sites(remaining)
            if not valid_add:
                continue
            add = self.rng.choice(valid_add)
            return stacking, tuple(sorted(remaining | {add}))
        return candidate

    def _layer_swap(
        self,
        candidate: Tuple[str, Tuple[int, ...]],
    ) -> Tuple[str, Tuple[int, ...]]:
        """Move a Li from one c-axis layer to a valid vacant site in a different layer."""
        stacking, config = candidate
        occupied = set(config)
        vacant = set(range(self.n_li_total)) - occupied

        occ_layers = [
            (i, [s for s in lyr if s in occupied])
            for i, lyr in enumerate(self.layers)
            if any(s in occupied for s in lyr)
        ]
        vac_layers = [
            (i, [s for s in lyr if s in vacant])
            for i, lyr in enumerate(self.layers)
            if any(s in vacant for s in lyr)
        ]
        if not occupied or not vacant or not occ_layers or not vac_layers:
            return self._vacancy_swap(candidate)

        for _ in range(100):
            src_idx, src_sites = self.rng.choice(occ_layers)
            tgt_candidates = [(i, s) for i, s in vac_layers if i != src_idx]
            if not tgt_candidates:
                return self._vacancy_swap(candidate)
            _, tgt_sites = self.rng.choice(tgt_candidates)
            remove = self.rng.choice(src_sites)
            remaining = occupied - {remove}
            remaining_list = list(remaining)
            valid_tgt_sites = [
                s for s in tgt_sites
                if s not in remaining and self._can_add_site(s, remaining_list)
            ]
            if not valid_tgt_sites:
                continue
            add = self.rng.choice(valid_tgt_sites)
            return stacking, tuple(sorted(remaining | {add}))
        return self._vacancy_swap(candidate)

    def _stacking_flip(
        self,
        candidate: Tuple[str, Tuple[int, ...]],
    ) -> Tuple[str, Tuple[int, ...]]:
        """Change stacking sequence while keeping the Li configuration fixed."""
        stacking, config = candidate
        if len(self.stacking_sequences) <= 1:
            return candidate
        alternatives = [m for m in self.stacking_sequences if m != stacking]
        return self.rng.choice(alternatives), config

    # ── Main loop ──────────────────────────────────────────────────────────

    def run(self) -> Tuple[List[Tuple[str, Tuple[int, ...]]], List[float]]:
        """
        Returns all evaluated candidates sorted by energy.

        The returned list is not limited to survivors. This ensures the final
        CIF top-20 is selected from all GA-evaluated candidates, not only from
        the survivor subset.
        """
        # Determine the valid Li-occupation pool under the same-layer Li-Li
        # distance constraint. For dense SOC levels, C(N,k) can be large while
        # the number of physically allowed hard-core configurations is small
        # or even zero.
        valid_occupied_pool = self._enumerate_valid_occupied_pool(
            limit=max(50000, self.pop_size * 500)
        )
        if not valid_occupied_pool:
            raise NoValidGACandidateError(
                f"No valid GA Li configuration exists with n_Li={self.n_li_keep} under the current "
                f"same-layer Li-Li distance constraint."
            )

        n_possible = len(valid_occupied_pool) * len(self.stacking_sequences)
        if self.valid_occupied_pool_limited:
            logger.warning(
                f"  GA valid Li-occupation pool reached enumeration limit; "
                f"using first {len(valid_occupied_pool):,} valid occupations as the sampling pool."
            )

        # Edge case: only one candidate exists.
        if n_possible == 1:
            only_candidate = (self.stacking_sequences[0], valid_occupied_pool[0])
            energy = self._energy(only_candidate)
            self.plot_data = [[(energy, "survivor")]]
            self.n_survivors_discovered = 1
            return [only_candidate], [energy]

        init_target = min(self.pop_size, n_possible)
        seen_init_keys: set = set()
        seen_init_exact: set = set()
        population: List[Tuple[str, Tuple[int, ...]]] = []
        pop_types: List[str] = []
        max_init_attempts = max(5000, init_target * 300)
        attempts = 0

        if self.motif_seeding_active:
            rh_f, facing_f, rand_f = normalize_fraction_triplet(
                self.motif_rh_fraction, self.motif_facing_fraction, self.motif_random_fraction
            )
            n_rh_target = int(round(init_target * rh_f))
            n_facing_target = int(round(init_target * facing_f))
            n_random_target = max(0, init_target - n_rh_target - n_facing_target)
            seed_plan = [("rh_seed", "rh", n_rh_target),
                         ("facing_seed", "facing", n_facing_target),
                         ("random", "random", n_random_target)]
            for label, motif_type, target in seed_plan:
                local_attempts = 0
                while pop_types.count(label) < target and len(population) < init_target and local_attempts < max_init_attempts:
                    local_attempts += 1
                    attempts += 1
                    try:
                        c = self._motif_candidate(motif_type)
                    except NoValidGACandidateError:
                        break
                    if self._append_unique_candidate(population, pop_types, c, label, seen_init_keys, seen_init_exact):
                        if label == "rh_seed":
                            self.n_motif_rh_seeded += 1
                        elif label == "facing_seed":
                            self.n_motif_facing_seeded += 1
                        else:
                            self.n_motif_random_seeded += 1

        # Fallback random fill — NOT counted as motif seeds even when motif
        # seeding is active, because these are plain random candidates used only
        # to reach the target population size.
        while len(population) < init_target and attempts < max_init_attempts:
            attempts += 1
            try:
                c = self._random_candidate()
            except NoValidGACandidateError:
                break
            self._append_unique_candidate(
                population, pop_types, c, "random", seen_init_keys, seen_init_exact
            )
        if not population:
            raise RuntimeError(
                f"Failed to initialize any valid GA candidate for n_Li={self.n_li_keep}. "
                "The same-layer Li-Li distance constraint may be too restrictive."
            )
        if len(population) < init_target:
            logger.warning(
                f"  GA initial population: requested {init_target}, but only "
                f"{len(population)} unique valid candidates were generated after "
                f"{attempts} attempts."
            )
        if self.motif_seeding_active:
            logger.info(
                f"  GA motif-seeded initial population: RH-like/alternating={self.n_motif_rh_seeded}, "
                f"facing-enhanced={self.n_motif_facing_seeded}, random={self.n_motif_random_seeded}; "
                f"uniform_vacancy_mode={self.uniform_vacancy_mode}"
            )

        global_best_energy = float("inf")
        all_survivors: Dict[Tuple[str, Tuple[int, ...]], float] = {}

        for gen in range(self.n_generations):
            energies = self._energies_for_population(population, generation=gen)
            min_e = min(energies)
            if min_e < global_best_energy:
                global_best_energy = min_e

            gen_plot: List[Tuple[float, str]] = []
            for c, e, t in zip(population, energies, pop_types):
                is_surv = e <= min_e + self.threshold_eV
                gen_plot.append((e, "survivor" if is_surv else t))
            self.plot_data.append(gen_plot)

            survivor_pairs = [
                (c, e) for c, e, t in zip(population, energies, pop_types)
                if e <= min_e + self.threshold_eV
            ]
            for c, e in survivor_pairs:
                all_survivors[c] = e

            survivor_configs = [c for c, _ in survivor_pairs]

            logger.debug(
                f"  Gen {gen + 1:3d}: min={min_e:.4f} eV  "
                f"survivors={len(survivor_configs)}  cached={len(self.energy_cache)}"
            )

            # Build next generation
            n_surv = len(survivor_configs)
            n_new = max(0, self.pop_size - n_surv)
            n_rand = max(1, int(n_new * self.frac_random)) if n_new > 0 else 0
            n_remaining = n_new - n_rand
            n_stack = int(n_remaining * self.frac_stacking_mutation) if len(self.stacking_sequences) > 1 else 0
            n_remaining -= n_stack
            n_facing = int(n_remaining * self.facing_mutation_prob) if self.motif_seeding_active else 0
            n_remaining -= n_facing
            n_vswap = n_remaining // 2
            n_lswap = n_remaining - n_vswap

            new_pop: List[Tuple[str, Tuple[int, ...]]] = []
            new_types: List[str] = []
            seen_gen_keys: set = set()
            seen_gen_exact: set = set()

            for c in survivor_configs:
                self._append_unique_candidate(
                    new_pop, new_types, c, "survivor", seen_gen_keys, seen_gen_exact
                )

            for _ in range(n_rand):
                self._append_unique_candidate(
                    new_pop, new_types, self._random_candidate(), "random",
                    seen_gen_keys, seen_gen_exact
                )

            src_pool = survivor_configs if survivor_configs else [self._random_candidate()]
            for _ in range(n_vswap):
                self._append_unique_candidate(
                    new_pop, new_types, self._vacancy_swap(self.rng.choice(src_pool)),
                    "vacancy_swap", seen_gen_keys, seen_gen_exact
                )
            for _ in range(n_lswap):
                self._append_unique_candidate(
                    new_pop, new_types, self._layer_swap(self.rng.choice(src_pool)),
                    "layer_swap", seen_gen_keys, seen_gen_exact
                )
            for _ in range(n_facing):
                base = self.rng.choice(src_pool)
                stacking, _ = base
                mutated = (stacking, self._facing_enhanced_occupied())
                if self._append_unique_candidate(
                    new_pop, new_types, mutated,
                    "facing_mutation", seen_gen_keys, seen_gen_exact
                ):
                    self.n_facing_mutations_generated += 1
            for _ in range(n_stack):
                self._append_unique_candidate(
                    new_pop, new_types, self._stacking_flip(self.rng.choice(src_pool)),
                    "stacking_flip", seen_gen_keys, seen_gen_exact
                )

            # If exact/hash duplicate filtering made the generation too small,
            # refill with random valid candidates up to the intended generation size.
            refill_target = min(self.pop_size, n_possible)
            refill_attempts = 0
            while len(new_pop) < refill_target and refill_attempts < max(1000, refill_target * 50):
                refill_attempts += 1
                self._append_unique_candidate(
                    new_pop, new_types, self._random_candidate(), "random",
                    seen_gen_keys, seen_gen_exact
                )

            population = new_pop
            pop_types = new_types

        self.n_survivors_discovered = len(all_survivors)

        logger.info(
            f"  GA complete: {len(self.energy_cache)} unique candidates evaluated "
            f"({self.n_survivors_discovered} survivors), "
            f"global best E = {global_best_energy:.4f} eV"
        )
        if self.n_candidate_key_duplicates_removed or self.n_candidate_coordinate_duplicates_removed:
            logger.info(
                f"  GA generation-level duplicate screening: "
                f"candidate-key duplicates removed={self.n_candidate_key_duplicates_removed}, "
                f"coordinate-hash duplicates removed={self.n_candidate_coordinate_duplicates_removed}"
            )
        if self.motif_seeding_active:
            logger.info(
                f"  GA motif statistics: RH-like seeds={self.n_motif_rh_seeded}, "
                f"facing seeds={self.n_motif_facing_seeded}, random seeds={self.n_motif_random_seeded}, "
                f"facing mutations accepted={self.n_facing_mutations_generated}, "
                f"uniform-vacancy rejections={self.n_uniform_vacancy_rejections}"
            )

        sorted_evaluated = sorted(self.energy_cache.items(), key=lambda x: x[1])
        configs = [c for c, _ in sorted_evaluated]
        energies_out = [e for _, e in sorted_evaluated]
        return configs, energies_out

